using namespace std;

ll dp[500001];
vector<ll> arr;
int n;

int recur(int idx, int thr){
    ll & ret = dp[idx];
    if(ret!=-1) return ret;
    ret = 1234567890123456789;
    if(idx-1>=0) ret = min(ret, recur(idx-2, thr) + arr[idx] - arr[idx-1]);
    if(idx-2>=0 && thr==0) ret = min(ret, recur(idx-3, 1) + arr[idx] - arr[idx-2]);
    return ret;
}

int main(){
    memset(dp, -1, sizeof dp);
    cin>>n;
    for(int i=0;i<n;i++){
        int x;cin>>x;
        arr.push_back(x);
    }
    sort(arr.begin(), arr.end());
    cout<<recur(n-1, 0);
}