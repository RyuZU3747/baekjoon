#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll fastpow(ll a, ll x, ll m){
    ll ret = 1;
    while(x){
        if(x%2){
            ret = ret*a%m;
        }
        a =  a * a%m;
        x /= 2;
    }
    return ret;
}

int main(){
    FASTIO;
    int lv;cin>>lv;
    if(lv<=5) cout<<fastpow(4,lv,MOD);
    else{
        ll pure = fastpow(8, lv, MOD);
        ll ans = pure;
        for(int i=1;i<=6;i++){
            ll up = 1;
            ll down = 1;
            for(int j=0;j<i;j++){
                up *= (lv-j);
                up %= MOD;
                down *= (j+1);
            }
            ll t = up * fastpow(down, MOD-2, MOD) % MOD;
            ans += pure*t;
            ans %= MOD;
        }
        cout<<ans;
    }
}