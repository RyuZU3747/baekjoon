#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int root[100001];
int capa[100001];
int gang[100001];
int sz[100001];
int hong;

int find(int x){
    return root[x] = root[x] == x ? x : find(root[x]);
}

void Union(int x, int y){
    int fx = find(x);
    int fy = find(y);
    if(fx>fy){
        root[fy]=root[fx];
        sz[fx] += sz[fy];
        capa[fx] += capa[fy];
        gang[fx] += gang[fy];
        sz[fy] = 0;
        capa[fy] = 0;
        gang[fy] = 0;
    }
    else{
        root[fx]=root[fy];
        sz[fy] += sz[fx];
        capa[fy] += capa[fx];
        gang[fy] += gang[fx];
        sz[fx] = 0;
        capa[fx] = 0;
        gang[fx] = 0;
    }
    return;
}

int main(){
    FASTIO;
    for(int i=0;i<100001;i++){
       root[i]=i;
       sz[i] = 1;
    }
    int n,m;cin>>n>>m;
    for(int i=1;i<=n;i++) cin>>capa[i];
    for(int i=1;i<=n;i++){
        cin>>gang[i];
        if(gang[i] > capa[i]) hong++;
    }
    for(int i=0;i<m;i++){
        int a;cin>>a;
        if(a==2) cout<<hong<<'\n';
        else{
            int b,c;cin>>b>>c;
            if(find(b)!=find(c)){
                int f = find(b);
                int s = find(c);
                if(capa[f] < gang[f]) hong -= sz[f];
                if(capa[s] < gang[s]) hong -= sz[s];
                Union(f,s);
                int af = find(b);
                if(capa[af] < gang[af]) hong += sz[af];
            }
        }
    }
}