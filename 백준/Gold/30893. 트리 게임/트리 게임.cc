using namespace std;

vector<int> graph[100001];
vector<int> route;
vector<int> tmp;
int n,s,e;

void dfs(int cur, int bef){
    if(cur==e){
        route = tmp;
        route.push_back(cur);
        return;
    }
    tmp.push_back(cur);
    for(auto nxt: graph[cur]){
        if(bef==nxt) continue;
        
        dfs(nxt, cur);
    }
    tmp.pop_back();
}

int main(){
    cin>>n>>s>>e;
    for(int i=0;i<n-1;i++){
        int a,b;cin>>a>>b;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }
    dfs(s, 0);
    int hu = 0;
    for(int i=1;i<route.size();i+=2){
        if(route[i]==e) break;
        if(graph[route[i]].size()>2) hu = 1;
    }
    if(hu) cout<<"Second";
    else cout<<"First";
}