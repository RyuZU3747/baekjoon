    int comb[22][22];
    memset(comb, 0, sizeof comb);
    for(int i=0;i<21;i++){
        comb[i][0] = 1;
        comb[i][i] = 1;
    }
    for(int i=1;i<21;i++){
        for(int j=1;j<=i;j++){
            comb[i][j] = comb[i-1][j-1] + comb[i-1][j];
        }
    }
    double dp[22][22];
    memset(dp, 0, sizeof dp);
    dp[0][0] = 1;
    for(int i=0;i<d;i++){
        for(int j=0;j<=m;j++){
            for(int k=0;k<=j && k<=c;k++){
                dp[i+1][j+c-k] += dp[i][j] * (comb[j][k] * comb[n-j][c-k]) / comb[n][c];
            }
        }
    }
