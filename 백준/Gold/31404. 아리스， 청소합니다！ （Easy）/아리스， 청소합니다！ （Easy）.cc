#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dh[4] = {-1, 0, 1, 0};
int dw[4] = {0, 1, 0, -1};
int chkB[65][65][5];
int h,w;
int r,c,d;
string A[65];
string B[65];
int mp[65][65];

int recur(int R, int C, int D, int len){
    if(R < 0 || C < 0 || R >= h || C >= w || len > 4*h*w) return 0;
    int ret = 0;
    if(mp[R][C] == 1){
        mp[R][C] = 0;
        D += A[R][C]-'0';
        D %= 4;
        int nr = R + dh[D];
        int nc = C + dw[D];
        ret = recur(nr, nc, D, 0) + 1;
        return ret;
    }
    else{
        D += B[R][C]-'0';
        D %= 4;
        int nr = R + dh[D];
        int nc = C + dw[D];
        int ret = recur(nr, nc, D, len+1);
        if(ret > 0) ret += 1;
        return ret;
    }
}

int main(){
    FASTIO;
    cin>>h>>w;
    cin>>r>>c>>d;
    for(int i=0;i<h;i++) cin>>A[i];
    for(int i=0;i<h;i++) cin>>B[i];
    for(int i=0;i<h;i++){
        for(int j=0;j<w;j++) mp[i][j] = 1;
    }
    cout<<recur(r,c,d, 0);
}