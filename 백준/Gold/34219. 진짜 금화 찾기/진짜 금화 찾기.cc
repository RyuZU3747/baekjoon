#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define FASTIO cin.tie(0), ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    int l=0, r=n;
    for(int _=0;_<3;_++){
        int t = ceil((r-l)/6.0);
        cout<<"? ";
        pii rng[6]={{n,-1},{n,-1},{n,-1},{n,-1},{n,-1},{n,-1}};
        int m = 0;
        for(int i=0;i<l;i++) cout<<0<<' ';
        for(int i=l;i<r;i++){
            int xx = (i-l)/t;
            cout<<xx<<' ';
            m += xx;
            rng[xx].first = min(rng[xx].first, i);
            rng[xx].second = max(rng[xx].second, i+1);
        }
        for(int i=r;i<n;i++) cout<<0<<' ';
        cout<<endl;
        int sum;
        cin>>sum;
        int x = max(0, sum - m*9);
        l = rng[x].first, r = rng[x].second;
        if(l==r-1){
            cout<<"! "<<l+1<<endl;
            break;
        }
    }
}