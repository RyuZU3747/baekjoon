#include <bits/stdc++.h>
#define ll long long int
#define swap(a,b) {ll c; c=a;a=b;b=c;}
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int cost[101][10001];

int main(){
	FASTIO;
	int k;cin>>k;
	int n,r;cin>>n>>r;
	vector<array<int, 3>> road[101];
	for(int i=0;i<r;i++){
		int s,l,d,t;cin>>s>>d>>l>>t;
		road[s].push_back({d,l,t});
	}
	for(int i=0;i<=n;i++){
		for(int j=0;j<=k;j++) cost[i][j] = 1234567890;
	}
	priority_queue<array<int, 3>> pq;
	pq.push({0,k,1});
	while(!pq.empty()){
		int curlen = -pq.top()[0];
		int curcos = pq.top()[1];
		int curidx = pq.top()[2];
		pq.pop();
		if(cost[curidx][curcos] < curlen) continue;
		for(auto nxt:road[curidx]){
			int nxtidx = nxt[0];
			int nxtlen = nxt[1];
			int nxtcos = nxt[2];
			if(curcos - nxtcos < 0) continue;
			if(cost[nxtidx][curcos-nxtcos] < curlen + nxtlen) continue;
			cost[nxtidx][curcos-nxtcos] = curlen + nxtlen;
			pq.push({-(curlen+nxtlen), curcos-nxtcos, nxtidx});
		}
	}
	int ans = 1234567890;
	for(int i=0;i<=k;i++){
		ans = min(ans, cost[n][i]);
	}
	if(ans==1234567890) cout<<-1;
	else cout<<ans;
}