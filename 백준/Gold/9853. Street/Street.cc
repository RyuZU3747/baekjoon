#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<ll> h;
ll n,k,t;
ll dp[501][501];
ll minh[501][501];

int main(){
    FASTIO;
    cin>>n>>k>>t;
    h.resize(n);
    for(auto &x: h) cin>>x;
    memset(minh, 0x3f, sizeof minh);
    for(int i=0;i<n;i++){
        for(int j=i;j<n;j++){
            for(int m=i;m<=j;m++) minh[i][j] = min(minh[i][j], h[m]);
        }
    }
    dp[0][1] = h[0];
    for(int i=0;i<n;i++){
        for(int j=1;j<=k;j++){
            if(i>0) dp[i][j] = max(dp[i][j], dp[i-1][j]);
            for(ll x=0;x<i;x++){
                int sz = min(t, i-x);
                dp[i][j] = max(dp[i][j], dp[x][j-1] + sz*minh[i-sz+1][i]);
            }
        }
    }
    // for(int i=0;i<n;i++) cout<<dp[i][1]<<' ';
    // cout<<'\n';
    // for(int i=0;i<n;i++) cout<<dp[i][2]<<' ';
    // cout<<'\n';
    cout<<dp[n-1][k];
}