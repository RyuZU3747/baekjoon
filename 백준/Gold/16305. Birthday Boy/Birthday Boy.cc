using namespace std;

int mnday[13] = {0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
vector<pii> days;

int main(){
    int n;cin>>n;
    for(int i=0;i<n;i++){
        char s[22];
        scanf(" %s", s);
        char das;
        int a,b;
        scanf("%2d-%2d",&a,&b);
        days.push_back({a,b});
    }
    sort(days.begin(), days.end(), [](pii a, pii b){
        if(a.first == b.first) return a.second < b.second;
        return a.first < b.first;
    });
    vector<int> difs;
    for(int i=0;i<n;i++){
        int t = 0;
        if(i==0){
            if(days[i].first == days[n-1].first){
                t += abs(days[i].second - days[n-1].second);
                t = 365 - t;
            }
            else{
                for(int j=days[i].first+1;j<days[n-1].first;j++){
                    t += mnday[j];
                }
                t += mnday[days[i].first] - days[i].second;
                t += days[n-1].second;
                t = 365 - t;
            }
        }
        else{
            if(days[i-1].first == days[i].first){
                t += days[i].second - days[i-1].second;
            }
            else{
                for(int j=days[i-1].first+1;j<days[i].first;j++){
                    t += mnday[j];
                }
                t += mnday[days[i-1].first] - days[i-1].second;
                t += days[i].second;
            }
        }
        difs.push_back(t);
    }
    int mx = 0;
    int mxidx = 0;
    for(int i=0;i<n;i++){
        if(mx < difs[i]){
            mx = difs[i];
            mxidx = i;
        }
    }
    vector<int> idxs;
    for(int i=0;i<n;i++){
        if(mx==difs[i]) idxs.push_back(i);
    }
    if(idxs.size()==1){
        int m = days[idxs[0]].first;
        int d = days[idxs[0]].second;
        if(d==1){
            m--;
            if(m==0) m = 12;
            d = mnday[m];
        }
        else d--;
        printf("%02d-%02d", m, d);
    }
    else{
        vector<pii> anss;
        for(auto i:idxs){
            int m = days[i].first;
            int d = days[i].second;
            if(d==1){
                m--;
                if(m==0) m = 12;
                d = mnday[m];
            }
            else d--;
            anss.push_back({m,d});
        }
        sort(anss.begin(), anss.end(), [](pii a, pii b){
            if(a.first == b.first) return a.second < b.second;
            return a.first < b.first;
        });
        pii out = anss[0];
        for(auto e: anss){
            if((e.first == 10 && e.second > 27) || (e.first > 10)){
                out = e;
                break;
            }
        }
        printf("%02d-%02d",out.first, out.second);
    }
}