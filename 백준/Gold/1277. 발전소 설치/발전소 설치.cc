#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int chk[1001][1001];

int main(){
    int n,w;cin>>n>>w;
    double m;cin>>m;
    vector<pii> bal;
    vector<pair<int, double>> graph[1001];
    for(int i=0;i<n;i++){
        int a,b;cin>>a>>b;
        bal.push_back({a,b});
    }
    for(int i=0;i<w;i++){
        int a,b;cin>>a>>b;
        a--,b--;
        graph[a].push_back({b,0});
        graph[b].push_back({a,0});
        chk[a][b] = 1;
        chk[b][a] = 1;
    }
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            if(i==j||chk[i][j]) continue;
            int dx = bal[i].first - bal[j].first;
            int dy = bal[i].second - bal[j].second;
            double dist = sqrt(1.0*dx*dx + 1.0*dy*dy);
            graph[i].push_back({j,dist});
            graph[j].push_back({i,dist});
            chk[i][j] = 1;
            chk[j][i] = 1;
        }
    }

    double e = 0.000001;
    double dist[1001];
    for(int i=0;i<n;i++) dist[i] = 1234567890123;
    priority_queue<pair<double, int>> pq;
    pq.push({0,0});
    dist[0] = 0;
    while(!pq.empty()){
        auto [dis, cur] = pq.top();
        dis = abs(dis);
        pq.pop();
        if(dist[cur] < dis) continue;
        for(auto [nxt, w] : graph[cur]){
            if(dist[nxt] - dis - w > e){
                dist[nxt] = dis+w;
                pq.push({-dist[nxt], nxt});
            }
        }
    }
    cout<<(ll)(dist[n-1] * 1000);
}