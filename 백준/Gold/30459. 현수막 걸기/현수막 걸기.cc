#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,m,R;cin>>n>>m>>R;
    double ans = 0;
    vector<int> pos(n);
    vector<int> pol(m);
    for(auto &x:pos) cin>>x;
    for(auto &x:pol) cin>>x;
    vector<int> len;
    sort(pos.begin(), pos.end());
    for(int i=0;i<n;i++){
        for(int j=i+1;j<n;j++){
            len.push_back(pos[j]-pos[i]);
        }
    }
    cout<<fixed;
    cout.precision(1);
    sort(len.begin(), len.end(), [](auto a, auto b){return a > b;});
    sort(pol.begin(), pol.end());
    int sz = len.size();
    if(pol[0] * len[sz-1] > R){
        cout<<-1;
        return 0;
    }
    for(auto p: pol){
        int l = 0, r = sz-1;
        while(l<=r){
            int mid = (l+r)/2;
            if(1.0 * len[mid] * p / 2.0 <= R){
                r = mid-1;
                ans = max(ans, 1.0 * len[mid] * p / 2.0);
            }
            else l = mid+1;
        }
    }
    cout<<ans;
}