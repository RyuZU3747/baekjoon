#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int arr[200001];
int dp[200001][3];
int n;

int recur(int cur, int turn){
    if(cur <= 0 || cur > n) return -1234567890;
    if(cur==n) return 0;
    int & ret = dp[cur][turn];
    if(ret != -1234567890) return ret;
    ret = -2000001;
    if(turn %2 == 0) ret = max(ret, recur(cur + arr[cur], turn) + 1);
    else ret = max(ret, recur(cur - arr[cur], turn) + 1);
    if(turn == 0) ret = max(ret, recur(cur - arr[cur], turn + 1) + 1);
    if(turn == 1) ret = max(ret, recur(cur + arr[cur], turn + 1) + 1);
    return ret;
}

int main(){
    FASTIO;
    cin>>n;
    for(int i=1;i<=n;i++) cin>>arr[i];
    for(int i=0;i<=n;i++){
        for(int j=0;j<3;j++) dp[i][j] = -1234567890;
    }
    int ans = recur(1, 0);
    cout<<(ans <= 0 ? -1 : ans);
}