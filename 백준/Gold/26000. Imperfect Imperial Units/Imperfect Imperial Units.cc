using namespace std;

map<string, map<string, long double>> changeto;

void recur(string from, string cur, long double val){
    changeto[from][cur] = val;
    changeto[cur][from] = 1.0/val;
    for(auto nxt: changeto[cur]){
        if(changeto[from].find(nxt.first) == changeto[from].end())
            recur(from, nxt.first, val * nxt.second);
    }
}

