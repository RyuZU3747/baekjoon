#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    ll n;cin>>n;
    ll sqn = sqrt(n);
    ll ans = 0;
    if(sqn * sqn == n) cout<<-1;
    else{
        set<pair<ll,ll>> squs;
        for(ll i = 1;i*i<n;i++){
            ll sqi = i*i;
            ll d = n - sqi;
            ll sqd = sqrt(d);
            if(sqd*sqd==d){
                squs.insert({min(sqi,d), max(sqi,d)});
            }
        }
        ll ans = squs.size();
        if(n%4==2) ans += 0;
        else if(n%4==0){
            ll yaksu = 0;
            for(ll i = 1;i*i<=n/4;i++){
                if(i*i==n/4) yaksu++;
                else if(n/4%i==0) yaksu += 2;
            }
            ans += floor((yaksu+1)/2);
        }
        else{
            ll yaksu = 0;
            for(ll i = 1;i*i<=n;i++){
                if(i*i==n) yaksu++;
                else if(n%i==0) yaksu += 2;
            }
            ans += floor((yaksu+1)/2);
        }
        cout<<ans;
    }
}