using namespace std;

int main(){
	int n,d;cin>>n>>d;
	int len[51][51];
	for(int i=0;i<n;i++){
		for(int j=0;j<n;j++){
			char a;cin>>a;
			if(a=='Y') len[i][j] = 1;
			else len[i][j] = 123456789;
		}
	}
	for(int k=0;k<n;k++){
		for(int i=0;i<n;i++){
			for(int j=0;j<n;j++){
				if(i!=j) len[i][j] = min(len[i][j], len[i][k] + len[k][j]);
			}
		}
	}
	int ans = -1;
	for(int i=0;i<n;i++){
		for(int j=0;j<n;j++){
			if(i!=j) ans = max(ans, len[i][j]);
		}
	}
	if(ans > 50) cout<<-1;
	else cout<<ans*d;
}