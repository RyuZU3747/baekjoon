#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    if(n<11) for(int i=0;i<n;i++) cout<<'a';
    else{
        string ans;
        int mx = -1;
        for(int i=2;i<=n-9;i++){
            map<char, int> cnt;
            cnt['R']=1;
            cnt['a']=1;
            cnt['i']=1;
            cnt['t']=1;
            cnt['h']=1;
            cnt['o']=1;
            cnt['r']=1;
            cnt['s']=1;
            cnt['e']=1;
            for(int j=0;j<n-9-i;j++){
                char mnc;
                int mn = 1234567;
                for(auto e: cnt){
                    if(mn > e.second){
                        mnc = e.first;
                        mn = e.second;
                    }
                }
                cnt[mnc]++;
            }
            string f = "Raithorse";
            string s = "";
            for(int j=0;j<9;j++){
                for(int k=0;k<cnt[f[j]];k++) s+=f[j];
                if(j==1) for(int k=0;k<i;k++) s+='b';
            }
            ll su = 1;
            for(int j=0;j<9;j++){
                su *= cnt[f[j]];
            }
            su *= i*(i-1)/2;
            if(su > mx){
                mx = su;
                ans = s;
            }
        }
        cout<<ans;
    }
}