#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    ll n,m,k;cin>>n>>m>>k;
    vector<ll> ani(n);
    for(auto &x:ani) cin>>x;
    sort(ani.begin(), ani.end());
    ll left = 0, right = n;
    ll ans = 0;
    while(left<=right){
        ll mid = (left+right) / 2;
        ll ok = 0;
        vector<ll> cur(ani.begin(), ani.begin() + mid);
        ll time = 0;
        for(ll i=mid-1;i>=0;i-=k) time += cur[i];
        if(time <= m) ok = 1;
        if(ok){
            left = mid+1;
            ans = mid;
        }
        else{
            right = mid-1;
        }
    }
    cout<<ans;
}