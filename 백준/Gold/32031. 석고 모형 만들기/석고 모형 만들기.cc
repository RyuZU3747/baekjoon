using namespace std;

int root[400001];

int find(int x){
   	return root[x] = root[x] == x ? x : find(root[x]);
}

void Union(int x, int y){
	int fx = find(x);
	int fy = find(y);
	if(fx > fy) swap(fx, fy);
	root[fy]=root[fx];
	return;
}

int block(int r, int c, int num){
	return 200*r + c + 40000*num;
}

