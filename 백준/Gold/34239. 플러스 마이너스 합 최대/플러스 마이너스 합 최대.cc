#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define FASTIO cin.tie(0), ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<ll> arr(n);
    ll ans = -1234567890123456789;
    for(auto &x: arr) cin>>x;
    for(auto e: arr) ans = max(ans, e);
    vector<ll> evep(n+1);
    vector<ll> oddp(n+1);
    ll emin = 0;
    ll omin = 12345678901234567;
    for(int i=0;i<n;i++){
        if(i%2==0){
            evep[i+1] = evep[i] + arr[i];
            oddp[i+1] = oddp[i] - arr[i];
        }
        else{
            evep[i+1] = evep[i] - arr[i];
            oddp[i+1] = oddp[i] + arr[i];
        }
        ans = max(ans, evep[i+1] - emin);
        ans = max(ans, oddp[i+1] - omin);
        if(i%2==0) emin = min(emin, evep[i]);
        else omin = min(omin, oddp[i]);
    }
    cout<<ans;
}