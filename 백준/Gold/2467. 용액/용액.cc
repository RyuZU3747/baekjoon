#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    ll n;cin>>n;
    vector<ll> arr(n);
    for(auto &x: arr) cin>>x;
    pll ans = {arr[0], arr[1]};
    ll mn = 2000000001;
    for(int i=0;i<n-1;i++){
        ll f = arr[i];
        auto p = upper_bound(arr.begin()+i+1, arr.end(), -f);
        ll p1 = *p;
        ll p2 = *(p-1);
        ll s1 = abs(p1 + f);
        ll s2 = abs(p2 + f);
        if(p != arr.end() && s1 <= mn){
            ans = {min(f,p1), max(f,p1)};
            mn = s1;
        }
        if(f!=p2 && s2 <= mn){
            ans = {min(f,p2), max(f,p2)};
            mn = s2;
        }
    }
    cout<<ans.first<<' '<<ans.second;
}