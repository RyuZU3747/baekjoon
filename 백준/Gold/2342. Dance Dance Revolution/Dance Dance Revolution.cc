#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll dp[100001][5][5];
ll p[5][5];

int main(){
    int arr[100001];
    int n;
    for(int i=1;;i++){
        cin>>arr[i];
        if(arr[i] == 0){
            n = i;
            break;
        }
    }
    p[0][1] = 2;
    p[0][2] = 2;
    p[0][3] = 2;
    p[0][4] = 2;
    p[1][1] = 1;
    p[1][2] = 3;
    p[1][3] = 4;
    p[1][4] = 3;
    p[2][1] = 3;
    p[2][2] = 1;
    p[2][3] = 3;
    p[2][4] = 4;
    p[3][1] = 4;
    p[3][2] = 3;
    p[3][3] = 1;
    p[3][4] = 3;
    p[4][1] = 3;
    p[4][2] = 4;
    p[4][3] = 3;
    p[4][4] = 1;

    for(int i=0;i<=n;i++){
        for(int left = 0;left<5;left++){
            for(int right = 0;right<5;right++){
                dp[i][left][right] = 1234567890;
            }
        }
    }
    dp[0][0][0] = 0;


    for(int i=1;i<=n;i++){
        for(int left = 0;left<5;left++){
            for(int right = 0;right<5;right++){
                int power = p[right][arr[i]];
                dp[i][left][arr[i]] = min(dp[i][left][arr[i]], dp[i-1][left][right] + power);
            }
        }
        for(int right = 0;right<5;right++){
            for(int left = 0;left<5;left++){
                int power = p[left][arr[i]];
                dp[i][arr[i]][right] = min(dp[i][arr[i]][right], dp[i-1][left][right] + power);
            }
        }
    }
    ll ans = 1234567890;
    for(int i=0;i<5;i++){
        for(int j=0;j<5;j++){
            ans = min(ans, dp[n][i][j]);
        }
    }
    cout<<ans;
}