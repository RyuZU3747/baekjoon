#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<pii> coin;
int dp[10001][101];
int t,k;

int recur(int cur, int idx){
    if(cur < 0) return 0;
    if(cur == 0) return 1;
    if(idx >= k) return 0;
    int & ret = dp[cur][idx];
    if(ret != -1) return ret;
    ret = 0;
    for(int i=0;i<=coin[idx].second;i++){
        ret += recur(cur - coin[idx].first * i, idx+1);
    }
    return ret;
}

int main(){
    FASTIO;
    memset(dp, -1, sizeof dp);
    cin>>k;
    cin>>t;
    for(int i=0;i<k;i++){
        int p,n;cin>>p>>n;
        coin.push_back({p,n});
    }
    cout<<recur(t, 0);
}