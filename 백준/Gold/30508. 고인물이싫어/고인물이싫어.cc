#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dr[4] = {0,0,1,-1};
int dc[4] = {1,-1,0,0};
int H[1001][1001];
int presum[1001][1001];

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    int h,w;cin>>h>>w;
    for(int i=1;i<=n;i++){
        for(int j=1;j<=m;j++) cin>>H[i][j];
    }
    int k;cin>>k;
    queue<array<int,3>> q;
    for(int i=0;i<k;i++){
        int a,b;cin>>a>>b;
        q.push({a,b,H[a][b]});
        H[a][b] = 0;
    }
    while(!q.empty()){
        auto [cr,cc,hei] = q.front();
        q.pop();
        for(int i=0;i<4;i++){
            int nr = cr + dr[i];
            int nc = cc + dc[i];
            if(nr<=0||nr>n||nc<=0||nc>m||H[nr][nc]==0) continue;
            if(H[nr][nc] < hei) continue;
            q.push({nr,nc,H[nr][nc]});
            H[nr][nc] = 0;
        }
    }
    for(int r=1;r<=n;r++){
        for(int c=1;c<=m;c++){
            presum[r][c] = presum[r-1][c] + presum[r][c-1] - presum[r-1][c-1] + H[r][c];
        }
    }
    int ans = 0;
    for(int r=1;r<=n;r++){
        int er = r + h - 1;
        for(int c=1;c<=m;c++){
            int ec = c + w - 1;
            if(er>n||ec>m) continue;
            int cnt = presum[er][ec] - presum[er][c-1] - presum[r-1][ec] + presum[r-1][c-1];
            if(cnt==0) ans++;
        }
    }
    cout<<ans;
}