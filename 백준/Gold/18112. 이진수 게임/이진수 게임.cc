#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int vis[1<<20];

int main(){
    FASTIO;
    string s,e;cin>>s>>e;
    int f = 0, t = 0;
    for(int i=0;i<s.length();i++){
        f |= (s[i]=='1') << (s.length() - i - 1);
    }
    for(int i=0;i<e.length();i++){
        t |= (e[i]=='1') << (e.length() - i - 1);
    }
    queue<pii> q;
    q.push({f,0});
    vis[f] = 1;
    int ans = 0;
    while(!q.empty()){
        auto [cur,cnt] = q.front();
        q.pop();
        if(cur == t){
            ans = cnt;
            break;
        }
        int msb = log2(cur);
        if(vis[cur+1] == 0){
            vis[cur+1] = 1;
            q.push({cur+1,cnt+1});
        }
        if(vis[max(0,cur-1)]==0){
            vis[max(0,cur-1)] = 1;
            q.push({max(0,cur-1),cnt+1});
        }
        for(int i=0;i<msb;i++){
            if(vis[cur^(1<<i)]==0){
                vis[cur^(1<<i)] = 1;
                q.push({cur^(1<<i), cnt+1});
            }
        }
    }
    cout<<ans;
}