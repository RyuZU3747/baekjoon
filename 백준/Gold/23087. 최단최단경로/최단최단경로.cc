#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000009
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll ans[100010];
vector<pair<ll,ll>> graph[100010];
pair<ll,ll> dist[100010];//dis, tran

int main(){
    FASTIO;
    ll n,m,x,y;cin>>n>>m>>x>>y;
    for(int i=0;i<m;i++){
        ll a,b,c;cin>>a>>b>>c;
        graph[a].push_back({b,c});
    }
    for(int i=0;i<100010;i++) dist[i].first = dist[i].second = 123456789012345678;

    priority_queue<array<ll,3>> pq;//dis trans cur
    pq.push({0,0,x});
    dist[x].first = 0;
    dist[x].second = 0;
    ans[x] = 1;
    while(!pq.empty()){
        ll dis = -pq.top()[0];
        ll tra = pq.top()[1];
        ll cur = pq.top()[2];
        pq.pop();
        if(dist[cur].first < dis || dist[cur].second < tra) continue;
        for(auto [nxt, len] : graph[cur]){
            if(dist[nxt].first > dis+len){
                dist[nxt].first = dis+len;
                dist[nxt].second = tra+1;
                ans[nxt] = ans[cur];
                pq.push({-(dis+len), tra+1, nxt});
            }
            else if(dist[nxt].first == dis+len && dist[nxt].second > tra+1){
                dist[nxt].second = tra+1;
                ans[nxt] = ans[cur];
                pq.push({-(dis+len), tra+1, nxt});
            }
            else if(dist[nxt].first == dis+len && dist[nxt].second == tra+1){
                ans[nxt] += ans[cur];
                ans[nxt] %= MOD;
            }
        }
    }
    if(dist[y].first == 123456789012345678) cout<<-1;
    else cout<<dist[y].first<<'\n'<<dist[y].second<<'\n'<<ans[y]%MOD;
}