#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    int n,m;cin>>n>>m;
    set<array<int,3>> s;
    for(int x=0;x<(n+1)*(m+1);x++){
        for(int y=0;y<(n+1)*(m+1);y++){
            for(int z=0;z<(n+1)*(m+1);z++){
                if(x==y || y==z || x==z) continue;
                int xr = x / (m+1);
                int xc = x % (m+1);
                int yr = y / (m+1);
                int yc = y % (m+1);
                int zr = z / (m+1);
                int zc = z % (m+1);
                if((xr-yr)*(zc-xc) == (xc-yc)*(zr-xr)) continue;
                array<int, 3> len;
                len[0] = (xr-yr)*(xr-yr)+(xc-yc)*(xc-yc);
                len[1] = (yr-zr)*(yr-zr)+(yc-zc)*(yc-zc);
                len[2] = (zr-xr)*(zr-xr)+(zc-xc)*(zc-xc);
                sort(len.begin(), len.end());
                s.insert(len);
            }   
        }
    }
    cout<<s.size();
}