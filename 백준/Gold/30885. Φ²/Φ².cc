#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#include <ext/pb_ds/assoc_container.hpp>
#include <ext/pb_ds/tree_policy.hpp>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;
using namespace __gnu_pbds;
#define ordered_set tree<int, null_type, less<int>, rb_tree_tag,tree_order_statistics_node_update>

int main(){
    FASTIO;
    int n;cin>>n;
    map<int, ll> arr;
    for(int i=1;i<=n;i++){
        int a;cin>>a;
        arr[i] = a;
    }
    while(arr.size()!=1){
        for(int i=1;i<=n;i++){
            if(arr.find(i)==arr.end()) continue;
            auto b = arr.upper_bound(i);
            auto f = arr.lower_bound(i);
            if(f!=arr.begin()) f--;
            int gf = 0;
            int gb = 0;
            if(b!=arr.end() && b != arr.find(i) && b->second <= arr[i]){
                gb = 1;
            }
            if(f != arr.find(i) && f->second <= arr[i]){
                gf = 1;
            }
            if(gb){
                arr[i] += b->second;
                arr.erase(b);
            }
            if(gf){
                arr[i] += f->second;
                arr.erase(f);
            }
        }
    }
    for(auto e: arr) cout<<e.second<<'\n'<<e.first;
}