#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll fastpow(ll a, ll x, ll m){
    ll ret = 1;
    while(x){
        if(x%2){
            ret = ret*a%m;
        }
        a =  a * a%m;
        x /= 2;
    }
    return ret;
}

int main(){
    ll n;cin>>n;
    ll a,b;cin>>a>>b;
    vector<pii> xpos;
    pii xd = {b-a,n};
    xpos.push_back({a*n,n});
    for(ll i=1;i<=n;i++){
        xpos.push_back({xpos[i-1].first + b-a,n});
    }
    vector<pii> ypos;
    for(auto x:xpos){
        pii f = {x.first - a*n, n};
        pii s = {x.first - b*n, n};
        ypos.push_back({f.first*s.first, n*n});
    }
    pii sig1 = {0,n*n*n};
    for(ll i=0;i<n;i++){
        sig1.first += xpos[i].first * ypos[i+1].first;
    }
    sig1.first += xpos[n].first * ypos[0].first;

    pii sig2 = {0,n*n*n};
    for(ll i=0;i<n;i++){
        sig2.first += xpos[i+1].first * ypos[i].first;
    }
    sig2.first += xpos[0].first * ypos[n].first;

    pii res = {abs(sig1.first - sig2.first), 2*n*n*n};
    ll dv = gcd(res.first, res.second);
    res.first /= dv;
    res.second /= dv;
    ll modinv = fastpow(res.second, MOD-2, MOD);
    cout<<(res.first * modinv)%MOD;
}