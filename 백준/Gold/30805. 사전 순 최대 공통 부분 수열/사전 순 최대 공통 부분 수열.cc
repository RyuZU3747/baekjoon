#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<int> arr(n);
    for(auto &x:arr) cin>>x;
    int m;cin>>m;
    vector<int> brr(m);
    for(auto &x:brr) cin>>x;
    
    int cursu = 0;
    int curaidx = 0;
    int curbidx = 0;
    vector<int> ans;
    while(1){
        if(curaidx == n || curbidx == m) break;
        int a = -1, b = -1;
        cursu = 0;
        for(int i=curaidx;i<n;i++){
            for(int j=curbidx;j<m;j++){
                if(arr[i]==brr[j]){
                    if(arr[i] > cursu){
                        cursu = arr[i];
                        a = i;b = j;
                    }
                }
            }
        }
        if(a==-1 && b==-1) break;
        if(cursu){
            ans.push_back(cursu);
            curaidx = a+1;
            curbidx = b+1;
        }
    }
    cout<<ans.size()<<'\n';
    for(auto e: ans) cout<<e<<' ';
}