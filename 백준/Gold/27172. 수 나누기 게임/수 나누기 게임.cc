#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

bool su[1000001];
int score[1000001];
int main(){
	FASTIO;
	int n;cin>>n;
	vector<int> suu(n);
	for(auto &x: suu){
		cin>>x;
		su[x] = 1;
	}
	for(int i=0;i<n;i++){
		int cur = suu[i];
		cur <<= 1;
		while(cur < 1000001){
			if(su[cur]==1){
				score[suu[i]]++;
				score[cur]--;
			}
			cur += suu[i];
		}
	}
	for(auto e: suu) cout<<score[e]<<' ';
}