#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int _;cin>>_;
    while(_--){
        int a,b;cin>>a>>b;
        int chk[10250];
        memset(chk, -1, sizeof chk);
        string ans = "";
        ans += to_string(a/b);
        ans += '.';
        a %= b;
        a *= 10;
        string s = "";
        int idx = 0;
        while(1){
            if(chk[a] != -1){
                for(int i=0; i<chk[a];i++) ans += s[i];
                ans += '(';
                for(int i=chk[a]; i<s.length();i++) ans += s[i];
                ans += ')';
                break;
            }
            chk[a] = idx++;
            s += to_string(a/b);
            a %= b;
            a *= 10;
        }
        cout<<ans<<'\n';
    }
}