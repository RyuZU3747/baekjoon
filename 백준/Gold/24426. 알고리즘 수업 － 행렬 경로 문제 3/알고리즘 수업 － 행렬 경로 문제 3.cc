#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dp1[1001][1001];
int dp2[1001][1001];
int dp3[1001][1001];
int mp[1001][1001];

int main(){
    FASTIO;
    int n;cin>>n;
    for(int i=1;i<=n;i++){
        for(int j=1;j<=n;j++) cin>>mp[i][j];
    }
    int r,c;cin>>r>>c;
    for(int i=1;i<=r;i++){
        for(int j=1;j<=c;j++){
            dp1[i][j] = mp[i][j] + max(dp1[i-1][j], dp1[i][j-1]);
        }
    }
    for(int i=r;i<=n;i++){
        for(int j=c;j<=n;j++){
            dp1[i][j] = mp[i][j] + max(dp1[i-1][j], dp1[i][j-1]);
        }
    }

    for(int i=1;i<=n;i++){
        for(int j=1;j<=n;j++){
            if(r==1&&i==1&&j>=c) dp2[i][j] = 0;
            else if(c==1&&j==1&&i>=r) dp2[i][j] = 0;
            else if(i==r+1&&j==c) dp2[i][j] = mp[i][j] + dp2[i][j-1];
            else if(i==r&&j==c+1) dp2[i][j] = mp[i][j] + dp2[i-1][j];
            else dp2[i][j] = mp[i][j] + max(dp2[i-1][j], dp2[i][j-1]);
        }
    }
    
    cout<<dp1[n][n]<<' '<<dp2[n][n];
}