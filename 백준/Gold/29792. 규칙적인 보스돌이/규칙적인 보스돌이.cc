#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,m,k;cin>>n>>m>>k;
    vector<ll> dam(n);
    vector<pair<ll,ll>> boss(k);
    vector<ll> money(n);
    for(auto &x:dam) cin>>x;
    for(auto &x:boss) cin>>x.first>>x.second;
    for(int cha = 0; cha<n;cha++){
        ll damage = dam[cha];
        for(ll i=0;i<(1LL<<(k+1)-1);i++){
            ll m = 0;
            ll dtmp = 0; 
            for(int bos = 0;bos<k;bos++){
                if(i & 1<<bos){
                    ll hp = boss[bos].first;
                    if(hp%damage > 0){
                        hp = ceil(1.0*hp/damage) * damage;
                    }
                    dtmp += hp;
                    m += boss[bos].second;
                }
            }
            if(dtmp <= damage * 900) money[cha] = max(money[cha],m);
        }
    }


    sort(money.begin(),money.end(),[](ll a, ll b){return a>b;});
    ll ans = 0;
    for(int i=0;i<m;i++) ans += money[i];
    cout<<ans;
}
