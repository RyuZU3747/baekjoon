#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<int> arr(n);
    for(auto &x: arr){
        cin>>x;
        x--;
    }
    int ans = 0;
    {
        vector<int> chk(n,0);
        for(int i=0;i<n;i++){
            if(chk[arr[i]]) continue;
            int cur = arr[i];
            ans++;
            do{
                chk[cur] = 1;
                cur = arr[cur];
            }while(!chk[cur]);
        }
        ans = n-ans;
    }
    reverse(arr.begin(), arr.end());
    {
        vector<int> chk(n,0);
        int cnt = 0;
        for(int i=0;i<n;i++){
            if(chk[arr[i]]) continue;
            int cur = arr[i];
            cnt++;
            do{
                chk[cur] = 1;
                cur = arr[cur];
            }while(!chk[cur]);
        }
        ans = min(ans, n-cnt);
    }

    cout<<n-2<<' '<<ans;
}