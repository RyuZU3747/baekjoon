#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<pll> graph[200010];
ll dist[200010];

int main(){
    ll n,m,k;cin>>n>>m>>k;
    memset(dist, 0x3F, sizeof dist);
    for(int i=0;i<m;i++){
        ll a,b,c;cin>>a>>b>>c;
        graph[a].push_back({b,c});
        graph[b].push_back({a,c});
        graph[a+100000].push_back({b+100000,c});
        graph[b+100000].push_back({a+100000,c});
    }
    for(int i=1;i<=n;i++){
        ll a;cin>>a;
        if(a==-1) continue;
        graph[i].push_back({i+100000,a*(k-1)});
        graph[i+100000].push_back({i,a*(k-1)});
    }
    priority_queue<array<ll,2>> pq;
    pq.push({0,1});
    dist[1] = 0;
    while(!pq.empty()){
        auto [cost, cur] = pq.top();
        pq.pop();
        cost = -cost;
        if(dist[cur] < cost) continue;
        for(auto [nxt, ncst]: graph[cur]){
            if(dist[nxt] > cost+ncst){
                dist[nxt] = cost+ncst;
                pq.push({-dist[nxt], nxt});
            }
        }
    }
    if(dist[n+100000] >= 4557430888798830398) cout<<-1;
    else cout<<dist[n+100000];
}