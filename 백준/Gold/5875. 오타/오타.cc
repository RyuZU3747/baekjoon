#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int psum[100001];

int main(){
    FASTIO;
    string s;cin>>s;
    int len = s.length();
    if(s[0]==')') psum[0] = -1;
    else psum[0] = 1;
    for(int i=1;i<len;i++){
        psum[i] = psum[i-1] + (s[i]==')' ? -1 : 1);
    }
    int mn = 100001;
    for(int i=0;i<len;i++){
        if(psum[i] < 0){
            mn = i;
            break;
        }
    }
    ll ans = 0;
    if(psum[len-1] > 0){ // 닫는괄호 추가
        for(int i=len-1;i>=0;i--){
            psum[i] -= 2;
            if(psum[i] < 0) break;
            if(s[i]=='(') ans++;
        }
    }
    else if(psum[len-1] < 0){
        for(int i=0;i<len;i++){
            if(s[i]==')' && i <= mn) ans++;
        }
    }
    cout<<ans;
}