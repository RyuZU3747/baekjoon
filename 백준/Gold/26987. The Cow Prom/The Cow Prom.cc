#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define FASTIO cin.tie(0), ios_base::sync_with_stdio(0)
using namespace std;

vector<int> graph[10010];
int degree[10010];
int vis[10010];

int bfs(int x){
    queue<int> q;
    q.push(x);
    vis[x] = 1;
    int ret = 1;
    while(!q.empty()){
        auto cur = q.front();
        q.pop();
        if(degree[cur]<2) ret = 0;
        for(auto nxt: graph[cur]){
            if(vis[nxt]) continue;
            vis[nxt] = 1;
            q.push(nxt);
        }
    }
    return ret;
}

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    for(int i=0;i<m;i++){
        int a,b;cin>>a>>b;
        degree[a]++;
        degree[b]++;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }
    int ans = 0;
    for(int i=1;i<=n;i++){
        if(vis[i]) continue;
        ans += bfs(i);
    }
    cout<<ans;
}