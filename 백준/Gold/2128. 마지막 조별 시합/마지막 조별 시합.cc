#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	int n,d,k;cin>>n>>d>>k;
    int stud[1001];
    memset(stud, 0, sizeof stud);
    for(int i=1;i<=n;i++){
        int a;cin>>a;
        for(int j=0;j<a;j++){
            int b;cin>>b;
            stud[i] |= 1<<(b-1);
        }
    }
    vector<int> chk;
    for(int i=0;i<15-k;i++) chk.push_back(0);
    for(int i=0;i<k;i++) chk.push_back(1);
    int ans = 0;
    do{
        int tmp = 0;
        int cur = 0;
        for(int i=0;i<chk.size();i++){
            cur |= (chk[i]<<i);
        }
        for(int i=1;i<=n;i++){
            if(stud[i]==0){
                tmp++;
                continue;
            }
            if((cur & stud[i]) == stud[i]) tmp++;
        }
        ans = max(ans, tmp);
    }while(next_permutation(chk.begin(), chk.end()));
    cout<<ans;
}