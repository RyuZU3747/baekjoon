using namespace std;

int root[100001];

int find(int x){
    return root[x] = root[x] == x ? x : find(root[x]);
}

void Union(int x, int y){
    int fx = find(x);
    int fy = find(y);
    if(fx>fy) root[fx]=root[fy];
    else root[fy]=root[fx];
    return;
}

int main(){
    for(int i=0;i<100001;i++) root[i]=i;
    int n,m;cin>>n>>m;
    for(int i=0;i<m;i++){
        int a,b;cin>>a>>b;
        Union(a,b);
    }
    int ans = 0;
    int b;cin>>b;
    for(int i=1;i<n;i++){
        int c;cin>>c;
        if(find(b)!=find(c)) ans++;
        b = c;
    }
    cout<<ans;
}