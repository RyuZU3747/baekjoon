#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<int> arr(n);
    int ok = 0;
    int plus = 0;
    for(int i=2;i<=n;i++){
        cout<<"? "<<1<<" * "<<i<<endl;
        char op;cin>>op;
        if(ok == 0 && op == '+'){
            ok = 1;
            cout<<"? 1 + "<<i<<endl;
            char s;cin>>s;
            if(s == '-') plus = 1;
        }
        if(op == '+') arr[i-1] = 0;
        else arr[i-1] = 1;
    }
    if(ok == 0){
        cout<<"? 2 + 3"<<endl;
        char s;cin>>s;
        if(s == '+') plus = 1;
    }
    cout<<"! ";
    for(auto e: arr){
        if(e == plus) cout<<'+'<<' ';
        else cout<<'-'<<' ';
    }
    cout<<endl;
}