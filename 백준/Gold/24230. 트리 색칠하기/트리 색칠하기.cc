#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<int> color;
vector<int> graph[200001];
int ans = 0;
void dfs(int cur, int curcol, int par){
    if(curcol != color[cur]) ans++;
    for(auto nxt: graph[cur]){
        if(nxt == par) continue;
        dfs(nxt, color[cur], cur);
    }
}

int main(){
    FASTIO;
    int n;cin>>n;
    color.resize(n+1);
    for(int i=1;i<=n;i++) cin>>color[i];
    for(int i=0;i<n-1;i++){
        int a,b;cin>>a>>b;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }
    dfs(1, 0, -1);
    cout<<ans;
}