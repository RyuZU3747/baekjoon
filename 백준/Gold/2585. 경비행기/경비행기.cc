#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<pair<int, double>> graph[1003];
double dist(pair<double, double> a, pair<double, double> b){
    return sqrt((a.first - b.first)*(a.first - b.first) + (a.second - b.second)*(a.second - b.second));
}

int main(){
    FASTIO;
    int n,k;cin>>n>>k;
    vector<pair<double, double>> p(n);
    for(auto &[x,y]: p) cin>>x>>y;
    for(int i=0;i<n;i++){
        graph[0].push_back({i+1, dist({0,0}, p[i])});
        graph[i+1].push_back({0, dist({0,0}, p[i])});
        graph[n+1].push_back({i+1, dist({10000,10000}, p[i])});
        graph[i+1].push_back({n+1, dist({10000,10000}, p[i])});
        for(int j=i+1;j<n;j++){
            graph[i+1].push_back({j+1, dist(p[i], p[j])});
            graph[j+1].push_back({i+1, dist(p[i], p[j])});
        }
    }
    
    int ans = 0;
    int left = 0, right = 1000;
    while(left<=right){
        int mid = left + right >> 1;
        vector<int> vis(n+2);
        queue<tuple<int, double, int>> q;
        q.push({0, 0, 0});
        vis[0] = 1;
        int ok = 0;
        while(!q.empty()){
            auto [cur, wei, cnt] = q.front();
            q.pop();
            if(cur == n+1){
                ok = 1;
                break;
            }

            if(cnt >= k) continue;
            for(auto [nxt, w]: graph[cur]){
                if(vis[nxt]) continue;
                if(w > mid*10) continue;
                vis[nxt] = 1;
                if(wei + w <= mid*10){
                    q.push({nxt, wei+w, cnt});
                }
                else{
                    q.push({nxt, w, cnt+1});
                }
            }
        }
        if(ok){
            right = mid-1;
            ans = mid;
        }
        else left = mid+1;
    }
    cout<<ans;
}