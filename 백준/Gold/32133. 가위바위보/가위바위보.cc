#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int n,m,k;
vector<string> arr;
string rsp = "RSP";
string ans;
string tmp;

int recur(int cur, set<int> lst){
	if(lst.size() > 0 && lst.size()<=k){
		if(ans.size() == 0) ans = tmp;
		else{
			if(ans.size() > tmp.size()) ans = tmp;
		}
		return 0;
	}
	if(cur == m) return -1;
	int ret = -1;
	for(int i=0;i<3;i++){
		set<int> nxt;
		for(auto idx: lst){
			if(arr[idx][cur]==rsp[(i+2)%3]){
				nxt.insert(idx);
			}
		}
		tmp += rsp[i];
		if(nxt.size() > 0) ret = max(ret, recur(cur + 1, nxt));
		tmp.pop_back();
	}
	return ret;
}

int main(){
	FASTIO;
	cin>>n>>m>>k;
	arr.resize(n);
	for(auto &x: arr) cin>>x;
	set<int> t;
	for(int i=0;i<n;i++) t.insert(i);
	int ret = recur(0, t);	
	if(ret == -1) cout<<-1;
	else{
		cout<<ans.size()<<'\n'<<ans;
	}
}