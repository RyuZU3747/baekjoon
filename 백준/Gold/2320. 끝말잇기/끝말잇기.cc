#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dp[5][1<<17];
string s[17];
int score[17];
int n;

int change(char a){
    return a=='A' ? 0 : (a=='E' ? 1 : (a=='I' ? 2 : (a=='O' ? 3 : 4)));
}

int recur(int lastidx, int cur){
    if(cur > (1<<n) - 1) return 0;
    int last = change(s[lastidx].back());
    int & ret = dp[last][cur];
    if(ret != -1) return ret;
    ret = 0;
    for(int i=0;i<n;i++){
        if(cur&(1<<i) || last != change(s[i].front())) continue;
        ret = max(ret, recur(i, cur | (1<<i)) + score[i]);
    }
    return ret;
}

int main(){
    cin>>n;
    for(int i=0;i<n;i++){
        cin>>s[i];
        score[i] = s[i].length();
    }
    int ans = 0;
    for(int i=0;i<n;i++){
        memset(dp, -1, sizeof dp);
        ans = max(ans, recur(i, 1<<i) + score[i]);
    }
    cout<<ans;
}