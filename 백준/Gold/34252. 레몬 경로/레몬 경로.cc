#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll fastpow(ll a, ll x, ll m){
    ll ret = 1;
    while(x){
        if(x%2){
            ret = ret*a%m;
        }
        a =  a * a%m;
        x /= 2;
    }
    return ret;
}

vector<pii> graph[100010];
int chk[100010];
int d[100010];
int dist[100010];

int main(){
	FASTIO;
    memset(chk, -1, sizeof chk);
    memset(dist, 0x3f, sizeof dist);
    int n,m;cin>>n>>m;
    for(int i=0;i<m;i++){
        int a,b,c;cin>>a>>b>>c;
        graph[a].push_back({b,c});
        graph[b].push_back({a,c});
    }
    queue<array<ll, 3>> q;
    q.push({1, 0, 0});
    chk[1] = 0;
    dist[1] = 0;
    while(!q.empty()){
        auto [cur, sum, dst] = q.front();
        q.pop();
        chk[cur]+=sum;
        chk[cur]%=MOD;
        d[cur]++;
        for(auto [nxt, cos]: graph[cur]){
            if(dist[nxt] <= dst) continue;
            dist[nxt] = dst+1;
            chk[nxt] = 0;
            q.push({nxt, sum+cos, dst+1});
        }
    }
    for(int i=2;i<=n;i++){
        cout<<(chk[i]*fastpow(d[i], MOD-2, MOD)%MOD)<<'\n';
    }
}