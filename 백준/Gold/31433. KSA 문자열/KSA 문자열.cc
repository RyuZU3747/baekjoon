#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    string s;cin>>s;
    string str = "";
    string ksa = "KSA";
    for(int i=0;i<s.length();i++) str += ("KSA"[i%3]);
    
    int ans = 1234567890;
    for(int st = 0;st<3;st++){
        int cnt = 0;
        int idx = st;
        for(int i=0;i<s.length();i++){
            if(s[i] == ksa[idx]){
                idx++;
                idx %= 3;
            }
            else cnt++;
        }
        cnt += max(0, st - cnt);
        ans = min(ans, cnt);
    }
    cout<<ans*2;
}