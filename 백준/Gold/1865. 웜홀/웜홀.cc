#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dist[501];
vector<array<int, 3>> graph;//u, v, 가중치
int N,M,W;

bool bf(int s){
    memset(dist, 0x3f, sizeof dist);
    for(int i=0;i<N;i++){
        for(auto [from, to, w]: graph){
            if(dist[to] > dist[from] + w){
                dist[to] = dist[from] + w;
                if(i == N-1) return true;
            }
        }
    }
    return false;
}

