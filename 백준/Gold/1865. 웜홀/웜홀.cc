    FASTIO;
    int _;cin>>_;
    while(_--){
        cin>>N>>M>>W;
        graph.clear();
        for(int i=0;i<M;i++){
            int a,b,w;cin>>a>>b>>w;
            graph.push_back({a,b,w});
            graph.push_back({b,a,w});
        }
        for(int i=0;i<W;i++){
            int a,b,w;cin>>a>>b>>w;
            graph.push_back({a,b,-w});
        }
        // cout<< (bf(1) == true ? "YES\n" : "NO\n");
        if(bf(1)) cout<<"YES\n";
        else cout<<"NO\n";
    }
