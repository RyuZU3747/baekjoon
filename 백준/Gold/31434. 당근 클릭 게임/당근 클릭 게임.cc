#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int n,k;
map<int, int> spd;

int recur(int cur, int c, int s){
    if(c < 0) return -1;
    if(cur == k) return c;

    int ret = recur(cur + 1, c + s, s);
    for(auto [a, b]: spd){
        if(c >= a) ret = max(ret, recur(cur + 1, c-a, s+b));
    }
    return ret;
}

int main(){
    FASTIO;
    cin>>n>>k;
    for(int i=0;i<n;i++){
        int a,b;cin>>a>>b;
        spd[a] = max(spd[a], b);
    }
    cout<<recur(0, 0, 1);
}