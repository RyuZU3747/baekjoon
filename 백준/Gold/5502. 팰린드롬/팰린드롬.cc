using namespace std;

string s;
int dp[5001][5001];

int recur(int left, int right){
    if(left>=right) return 0;
    int & ret = dp[left][right];
    if(ret != -1) return ret;
    ret = 1234567890;
    if(s[left]==s[right]){
        ret = min(ret, recur(left+1, right-1));
    }
    else{
        ret = min(recur(left+1,right)+1, recur(left, right-1)+1);
    }
    return ret;
}

