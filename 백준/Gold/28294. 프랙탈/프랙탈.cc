#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll fastpow(ll a, ll x, ll m){
    ll ret = 1;
    while(x){
        if(x%2){
            ret = ret*a%m;
        }
        a =  a * a%m;
        x /= 2;
    }
    return ret;
}

int main(){
    FASTIO;
    int n,a;cin>>n>>a;
    ll ans = (fastpow(n,a,MOD) - fastpow(n,a-1,MOD) + MOD)%MOD + fastpow(n-1,a,MOD);
    ans %= MOD;
    ll modinv = fastpow(n, MOD-2, MOD);
    ll r_1 = (1 - modinv + MOD) % MOD;
    ans += ((fastpow(n,a,MOD) - fastpow(n,a-1,MOD) + MOD) % MOD) * (r_1 * (fastpow(r_1,a-1,MOD)-1+MOD)%MOD)%MOD * fastpow(r_1-1, MOD-2, MOD) % MOD;
    ans %= MOD;
    cout<<((n%MOD) * (ans%MOD)) % MOD;
}