#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<char> stk;
    vector<pll> su;
    for(int i=0;i<n;i++){
        char a;cin>>a;
        if(a=='('){
            stk.push_back(a);
        }
        else if(a==')'){
            if(stk.size() < 4){
                cout<<-1;
                return 0;
            }
            for(int j=0;j<3;j++){
                char t = stk.back();
                stk.pop_back();
                if(t != 's'){
                    cout<<-1;
                    return 0;
                }
            }
            char t = stk.back();
            stk.pop_back();
            if(t!='('){
                cout<<-1;
                return 0;
            }
            auto thr = su.back();
            su.pop_back();
            auto two = su.back();
            su.pop_back();
            auto one = su.back();
            su.pop_back();
            pll ps = {two.first * thr.second, two.second * thr.first};
            ps = {one.first * ps.second + one.second * ps.first, one.second * ps.second};
            ll g = gcd(ps.first, ps.second);
            ps.first /= g;
            ps.second /= g;
            su.push_back(ps);
            stk.push_back('s');
        }
        else{
            su.push_back({a-'0', 1});
            stk.push_back('s');
        }
    }
    if(su.size() != 1){
        cout<<-1;
        return 0;
    }
    cout<<su.back().first<<' '<<su.back().second;
}