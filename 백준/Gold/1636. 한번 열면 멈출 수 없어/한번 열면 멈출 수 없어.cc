#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<pii> q(n);
    for(auto &x: q) cin>>x.first>>x.second;
    int ans = 1234567890;
    vector<int> lst(n);
    for(int st = q[0].first;st<=q[0].second;st++){
        int cur = st;
        int cnt = 0;
        vector<int> l;
        l.push_back(cur);
        for(int i=1;i<n;i++){
            auto [s,e] = q[i];
            if(cur >= s && cur <= e){
                l.push_back(cur);
            }
            else if(cur<=s){
                cnt += s - cur;
                cur = s;
                l.push_back(cur);
            } 
            else{
                cnt += cur - e;
                cur = e;
                l.push_back(cur);
            }
        }
        if(ans > cnt){
            ans = cnt;
            lst = l;
        }
    }
    cout<<ans<<'\n';
    for(auto e: lst) cout<<e<<'\n';
}