#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll dp[100001];

int main(){
    FASTIO;
    int n,k;cin>>n>>k;
    vector<int> arr(n);
    for(auto &x:arr) cin>>x;
    int left = 0, right = 0;
    ll cursum = arr[0];
    while(left<n&&right<n){
        if(right!=0) dp[right] = max(dp[right], dp[right-1]);

        if(cursum >= k){
            dp[right] = max(dp[right], dp[left-1] + cursum - k);

            if(left==right){
                left++;
                cursum = arr[++right];
                continue;
            }
            cursum -= arr[left++];
        }
        else{
            cursum += arr[++right];
        }
    }
    cout<<dp[n-1];
}