#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int mp[1005][1005];
int chk[1005][1005];
int dr[4] = {0,0,1,-1};
int dc[4] = {1,-1,0,0};

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    for(int i=1;i<=n;i++){
        for(int j=1;j<=m;j++){
            cin>>mp[i][j];
        }
    }
    pii s,e;
    cin>>s.first>>s.second;
    cin>>e.first>>e.second;
    memset(chk, 0, sizeof chk);
    queue<pii> q;
    set<pii> st, ed;
    q.push({s.first,s.second});
    chk[s.first][s.second] = 1;
    while(!q.empty()){
        auto [r,c] = q.front();
        q.pop();
        st.insert({r,c});
        if(r == e.first && c == e.second){
            cout<<0;
            return 0;
        }
        for(int i=0;i<4;i++){
            int nr = r + dr[i];
            int nc = c + dc[i];
            if(mp[nr][nc]==0 || chk[nr][nc]) continue;
            chk[nr][nc] = 1;
            q.push({nr,nc});
        }
    }
    q.push({e.first,e.second});
    chk[e.first][e.second] = 1;
    while(!q.empty()){
        auto [r,c] = q.front();
        q.pop();
        ed.insert({r,c});
        for(int i=0;i<4;i++){
            int nr = r + dr[i];
            int nc = c + dc[i];
            if(mp[nr][nc]==0 || chk[nr][nc]) continue;
            chk[nr][nc] = 1;
            q.push({nr,nc});
        }
    }
    memset(chk, 0, sizeof chk);
    queue<array<int, 3>> q2;
    for(auto x: st){
        q2.push({x.first, x.second, 0});
        chk[x.first][x.second] = 1;
    }
    while(!q2.empty()){
        auto [r,c,w] = q2.front();
        q2.pop();
        if(ed.count({r,c})){
            cout<<w;
            return 0;
        }
        for(int i=0;i<4;i++){
            int nr = r + dr[i];
            int nc = c + dc[i];
            if(nr<1||nr>n||nc<1||nc>m|| chk[nr][nc]) continue;
            chk[nr][nc] = 1;
            q2.push({nr,nc,w+1});
        }
    }
}