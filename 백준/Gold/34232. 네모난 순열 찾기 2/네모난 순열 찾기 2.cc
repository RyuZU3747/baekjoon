#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define FASTIO cin.tie(0), ios_base::sync_with_stdio(0)
using namespace std;

vector<pii> su;

int main(){
    FASTIO;
    int n;cin>>n;
    su.resize(n*n+1);
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            int a;cin>>a;
            su[a] = {i,j};
        }
    }
    pii rran = {su[1].first, su[1].first};
    pii cran = {su[1].second, su[1].second};

    ll ans = 1;
    for(int i=2;i<=n*n;i++){
        rran.first = min(rran.first, su[i].first);
        rran.second = max(rran.second, su[i].first);
        cran.first = min(cran.first, su[i].second);
        cran.second = max(cran.second, su[i].second);
        int r = rran.second - rran.first + 1;
        int c = cran.second - cran.first + 1;
        int sz = r*c;
        if(sz == i){
            ans++;
        }
    }
    cout<<ans;
}