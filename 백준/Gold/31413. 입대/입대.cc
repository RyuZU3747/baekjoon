#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll N,M,A,D;
vector<ll> arr;
ll dp[1001][1001];

ll recur(ll day, ll cnt){
    if(day > N) return 0;
    ll & ret = dp[day][cnt];
    if(ret != -1) return ret;
    ret = 0;
    if(cnt>0) ret = max(ret, recur(day+D, cnt - 1) + A);
    ret = max(ret, recur(day+1, cnt) + arr[day]);
    return ret;
}

int main(){
    FASTIO;
    cin>>N>>M;
    arr.resize(N+1);
    for(ll i=1;i<=N;i++) cin>>arr[i];
    cin>>A>>D;
    ll ans = 1234567890123456789;
    ll left = 0, right = N;
    while(left<=right){
        ll mid = left+right>>1;
        memset(dp, -1, sizeof dp);
        ll ret = recur(1, mid);
        if(ret >= M){
            right = mid - 1;
            ans = min(ans, mid);
        }
        else left = mid + 1;
    }
    if(ans == 1234567890123456789) cout<<-1;
    else cout<<ans;
}