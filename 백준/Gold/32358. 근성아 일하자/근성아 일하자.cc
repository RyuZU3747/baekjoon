#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    int cur = 0;
    ll ans = 0;
    set<int> trash;
    while(n--){
        int a;cin>>a;
        if(a==1){
            int b; cin>>b;
            trash.insert(b);
        }
        else{
            while(trash.size()>0){
                auto r = trash.lower_bound(cur);
                auto l = --trash.lower_bound(cur);
                if(r == trash.end()){
                    ans += abs(cur - *l);
                    cur = *l;
                    trash.erase(l);
                }
                else if(r == trash.begin()){
                    ans += abs(*r - cur);
                    cur = *r;
                    trash.erase(r);
                }
                else{
                    if(*r - cur < cur - *l){
                        ans += abs(*r - cur);
                        cur = *r;
                        trash.erase(r);
                    }
                    else{
                        ans += abs(cur - *l);
                        cur = *l;
                        trash.erase(l);
                    }
                }
            }
        }
    }
    cout<<ans;
}