#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<pll> con(n);
    for(auto &[x,y]: con) cin>>x>>y;
    sort(con.begin(), con.end(), [](pll a, pll b){return a.first < b.first;});
    vector<ll> fb(n);
    ll fbsum = 0;
    vector<ll> bf(n);
    ll bfsum = 0;
    ll sum = 0;
    for(int i=0;i<n;i++){
        sum += con[i].second;
        fb[i] = con[n-1].first - con[i].first;
        fbsum += fb[i];
    }
    for(int i=0;i<n;i++){
        bf[i] = con[i].first - con[0].first;
        bfsum += bf[i];
    }
    ll ans = 6546546546546546546;
    for(int i=1;i<n-1;i++){
        ll cur = sum - con[i].second;
        ll fd = fbsum - fb[i];
        ll bd = bfsum - bf[i];
        ans = min(ans, cur + fd);
        ans = min(ans, cur + bd);
    }
    //i == 0 eliminate
    ll t = 0;
    for(int i=1;i<n;i++){
        t += con[i].second;
        t += con[n-1].first - con[i].first;
    }
    ans = min(ans, t);
    t = 0;
    for(int i=1;i<n;i++){
        t += con[i].second;
        t += con[i].first - con[1].first;
    }
    ans = min(ans, t);
    //i==n-1 elim
    t = 0;
    for(int i=0;i<n-1;i++){
        t += con[i].second;
        t += con[n-2].first - con[i].first;
    }
    ans = min(ans, t);
    t = 0;
    for(int i=0;i<n-1;i++){
        t += con[i].second;
        t += con[i].first - con[0].first;
    }
    ans = min(ans, t);

    cout<<ans;
}