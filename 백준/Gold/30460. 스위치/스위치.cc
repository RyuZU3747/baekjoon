#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dp[200010];
int arr[200010];

int recur(int cur){
    if(cur <= 0) return 0;
    int & ret = dp[cur];
    if(ret != -1234567890) return ret;
    ret = max(ret, recur(cur-1) + arr[cur]);
    if(cur - 3 >= 0) ret = max(ret, recur(cur-3) + 2*arr[cur-2] + 2*arr[cur-1] + 2*arr[cur]);
    return ret;
}

int main(){
    FASTIO;
    int n;cin>>n;
    for(int i=1;i<=n;i++) cin>>arr[i];
    arr[n+1] = 0;
    arr[n+2] = 0;
    arr[n+3] = 0;
    for(int i=0;i<=n+3;i++) dp[i] = -1234567890;
    cout<<recur(n+3);
}