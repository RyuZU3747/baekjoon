#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<pii> graph[501];

int main(){
    int n;cin>>n;
    for(int i=0;i<n;i++){
        int su;cin>>su;
        for(int j=1;j<su;j++){
            graph[j+i*50].push_back({i*50+j-1, 1});
            graph[j+i*50-1].push_back({i*50+j, 1});
        }
    }
    int m;cin>>m;
    for(int i=0;i<m;i++){
        int u1,u2,v1,v2;cin>>u1>>u2>>v1>>v2;
        u1--;
        u2--;
        v1--;
        v2--;
        graph[u1*50+u2].push_back({v1*50+v2, -1});
        graph[v1*50+v2].push_back({u1*50+u2, -1});
    }
    int k;cin>>k;
    for(int i=0;i<k;i++){
        int t,u1,u2,v1,v2;cin>>t>>u1>>u2>>v1>>v2;
        u1--;
        u2--;
        v1--;
        v2--;
        int st = u1*50+u2;
        int ed = v1*50+v2;
        int dist[501];
        memset(dist, 123456789, sizeof dist);
        dist[st] = 0;
        priority_queue<pii> pq;
        pq.push(make_pair(0,st));
        while(!pq.empty()){
            int cur = pq.top().second;
            int dis = -pq.top().first;
            pq.pop();
            for(auto x: graph[cur]){
                int next = x.first;
                int wei = x.second;
                if(wei == -1) wei = t;
                if(dist[next] > dis+wei){
                    dist[next] = dis+wei;
                    pq.push(make_pair(-(dis+wei),next));
                }
            }
        }
        cout<<dist[ed]<<'\n';
    }
}