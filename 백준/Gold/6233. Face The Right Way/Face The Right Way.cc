#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<int> arr(n);
    for(int i=0;i<n;i++){
        char a;cin>>a;
        if(a=='B') arr[i] = 0;
        else arr[i] = 1;
    }
    int k, m = 1234567890;
    int chk[5001];
    for(int sz = n;sz>=1;sz--){
        memset(chk, 0, sizeof chk);
        vector<int> tmp = arr;
        int t = 0;
        int cur = 0;
        
        for(int i=0;i<n;i++){
            if(chk[i] == 1) cur--;
            tmp[i] += cur;
            if(tmp[i]%2==0 && i <= n-sz){
                cur++;
                tmp[i]++;
                t++;
                chk[i+sz] = 1;
            }
        }
        int no = 0;
        for(int i=0;i<n;i++){
            if(tmp[i]%2==0) no = 1;
        }
        if(no == 0){
            k = sz;
            m = t;
            break;
        }
    }
    cout<<k<<' '<<m<<'\n';
}