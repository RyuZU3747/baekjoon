#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    ll l,r,x,y;cin>>l>>r>>x>>y;
    if(l>r) swap(l,r);
    if(y<0||y>=abs(x)){
        cout<<"Unknwon Number";
        return 0;
    }
    x = abs(x);
    ll p = l;
    ll ans = 0;
    int cnt = 0;
    if((p%x+x)%x<y) p += y-(p%x+x)%x;
    else if((p%x+x)%x>y) p += x-(p%x+x)%x + y;
    while(p<=r){
        if((p%x+x)%x==y){
            if(cnt!=0){
                cout<<"Unknwon Number";
                return 0;
            }
            ans = p;
            cnt++;
        }
        p+=x;
    }
    if(cnt==0) cout<<"Unknwon Number";
    else cout<<ans;
}