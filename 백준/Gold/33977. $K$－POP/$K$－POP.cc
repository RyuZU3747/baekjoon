#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int k;cin>>k;
    pii mind = {1,k};
    int d = k-1;
    for(int i=1;i*i<=k;i++){
        if(k%i==0){
            int a = i, b = k/i;
            if(b-a < d) mind = {a,b};
        }
    }
    cout<<mind.first+mind.second<<'\n';
    for(int i=1;i<mind.second;i++){
        cout<<i<<' '<<i+1<<'\n';
    }
    int cur = mind.second;
    int cnt = 0;
    for(int i=mind.second+1;i<=mind.second+mind.first;i++){
        cout<<cur<<' '<<i<<'\n';
        cnt++;
        if(cur == mind.second){
            if(cnt>1) cur--;
        }
        else cur--;
    }
}