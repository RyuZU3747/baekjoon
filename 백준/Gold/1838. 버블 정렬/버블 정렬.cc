#include <bits/stdc++.h>
#define ll long long int
#define MOD1 1000000007
#define MOD2 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<pii> arr;
    map<int, int> idx;
    for(int i=0;i<n;i++){
        int a;cin>>a;
        arr.push_back({a,i});
        idx[a] = i;
    }
    vector<pii> brr = arr;
    sort(brr.begin(), brr.end());
    int ans = 0;
    for(int i=0;i<n;i++){
        ans = max(ans, idx[brr[i].first] - i);
    }
    cout<<ans;
}