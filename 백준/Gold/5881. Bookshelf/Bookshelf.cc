#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    int n,l;cin>>n>>l;
    vector<pii> book(n+1);
    for(int i=1;i<=n;i++) cin>>book[i].first>>book[i].second;
    int dp[2002];
    memset(dp, 0x3f, sizeof dp);
    dp[0] = 0;
    for(int i=1;i<=n;i++){
        int hei = 0;
        int wid = 0;
        for(int j=i-1;j>=0;j--){
            wid += book[j+1].second;
            hei = max(hei, book[j+1].first);
            if(wid <= l) dp[i] = min(dp[i], hei + dp[j]);
        }
    }
    cout<<dp[n];
}