#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<array<int, 3>> arr(n);
    for(int i=0;i<n;i++){
        arr[i][0] = i;
        cin>>arr[i][1]>>arr[i][2];
    }
    sort(arr.begin(),arr.end(),[](array<int,3> a, array<int,3> b){
        if(a[2] == b[2]) return a[1] < b[1];
        return a[2] < b[2];
    });
    map<int, int> col;
    ll asum = 0;
    vector<pii> ans;
    int tsum = 0;
    int bef = -1;
    int ci = 0;
    int cb = -1;
    for(auto [i,c,v]:arr){
        if(bef != v){
            asum += tsum;
            tsum = 0;
            col[cb] += ci;
            ci = 0;
        }
        if(cb != c){
            col[cb] += ci;
            ci = 0;
        }
        ans.push_back({i, asum - col[c]});
        ci += v;
        cb = c;
        tsum += v;
        bef = v;
    }
    sort(ans.begin(), ans.end());
    for(auto [a,b]:ans) cout<<b<<'\n';
}
