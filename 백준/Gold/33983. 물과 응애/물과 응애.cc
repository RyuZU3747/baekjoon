#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    string s;cin>>s;
    int hcnt = 0, ocnt = 0;
    for(int i=0;i<n;i++){
        if(s[i]=='H') hcnt++;
        else ocnt++;
    }
    map<pii, int> cnt;
    if(hcnt!=ocnt*2) cout<<"mix";
    else{
        int no = 0;
        int curh = 0;
        int curo = 0;
        for(int i=0;i<n;i++){
            if(s[i]=='O'){
                pii o = {curo, ocnt-curo-1};
                curo++;
                pii h = {curh, hcnt-curh};
                if(h.first < o.first+1) no = 1;
                if(h.second < o.second+1) no = 1;
            }
            else curh++;
        }
        if(no) cout<<"mix";
        else cout<<"pure";
    }
}