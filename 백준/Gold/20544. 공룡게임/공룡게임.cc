#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll dp[3][3][1001][2];
int n;

ll recur(int cur, int curh, int befh, int is2){
    if(cur == n-1){
        return is2;
    }
    ll & ret = dp[befh][curh][cur][is2];
    if(ret != 0) return ret;
    if(curh==0){
        ret += recur(cur+1, 0, curh, is2) % MOD;
        ret += recur(cur+1, 1, curh, is2) % MOD;
        ret += recur(cur+1, 2, curh, 1) % MOD;
    }
    if(curh==1){
        if(befh == 0){
            ret += recur(cur+1, 0, curh, is2) % MOD;
            ret += recur(cur+1, 1, curh, is2) % MOD;
            ret += recur(cur+1, 2, curh, 1) % MOD;
        }
        else ret += recur(cur+1, 0, curh, is2) % MOD;
    }
    if(curh==2){
        if(befh == 0){
            ret += recur(cur+1, 0, curh, is2) % MOD;
            ret += recur(cur+1, 1, curh, is2) % MOD;
        }
        else ret += recur(cur+1, 0, curh, is2) % MOD;
    }
    return ret % MOD;
}

int main(){
    FASTIO;
    cin>>n;
    ll ans = 0;
    ans += recur(0,0,0,0);
    cout<<ans % MOD;
}