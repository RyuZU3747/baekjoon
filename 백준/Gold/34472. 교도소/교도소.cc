#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define FASTIO cin.tie(0), ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    ll n;cin>>n;
    vector<ll> arr(n);
    ll sum = 0;
    for(auto &x: arr){
        cin>>x;
        sum += x;
    }
    ll su = sum/n;
    ll cur = 0;
    ll ans = 0;
    for(ll i=0;i<2*n;i++){
        auto & e = arr[i%n];
        if(e > su){
            cur += e-su;
            e = su;
        }
        if(e < su && cur >= 0){
            ll t=min(su-e, cur);
            e += t;
            cur -= t;
        }
        ans += cur;
    }
    cout<<ans;
}