#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int mp[1002][1002];
int vis[1002][1002];
int dr[4] = {0,0,1,-1};
int dc[4] = {1,-1,0,0};

int main(){
    FASTIO;
    memset(mp, -1, sizeof mp);
    int m,n;cin>>m>>n;
    queue<array<int, 3>> q; // 행, 열, 날짜
    for(int i=1;i<=n;i++){
        for(int j=1;j<=m;j++){
            cin>>mp[i][j];
            if(mp[i][j] == 1){
                q.push({i,j,0});
                vis[i][j] = 1;
            }
        }
    }
    int ans = 0;
    while(!q.empty()){
        auto [cr, cc, cnt] = q.front();
        q.pop();
        ans = max(ans, cnt);
        for(int i=0;i<4;i++){
            int nr = cr + dr[i];
            int nc = cc + dc[i];
            if(mp[nr][nc] == -1 || vis[nr][nc]) continue;
            vis[nr][nc] = 1;
            mp[nr][nc] = 1;
            q.push({nr,nc,cnt+1});
        }
    }
    for(int i=1;i<=n;i++){
        for(int j=1;j<=m;j++){
            if(mp[i][j] == 0) ans = -1;
        }
    }
    cout<<ans;
}