int ett(int cur){
	indegree.push_back(cur);
	s[cur] = indegree.size()-1;
	for(auto e:graph[cur]){
		outdegree[cur] += ett(e);
	}
	return outdegree[cur];
}

void make_segtree(int node, int left, int right){
	if(left==right){
		tree[node].push_back(arr[indegree[left]]);
		return;
	}
	make_segtree(node*2,left,(left+right)/2);
	make_segtree(node*2+1,(left+right)/2+1,right);
	tree[node].resize((right-left)+1);
	merge(tree[node*2].begin(), tree[node*2].end(), tree[node*2+1].begin(),tree[node*2+1].end(), tree[node].begin());
	sort(tree[node].begin(),tree[node].end());
}

ll query(int node, int left, int right, ll first, ll second, ll value){
	if(second<left||right<first) return 0;
	if(first<=left&&right<=second){
		// cout<<left<<' '<<right<<'\n';
		// cout<<node<<": ";
		// for(auto e: tree[node]) cout<<e<<' ';
		// cout<<(upper_bound(tree[node].begin(), tree[node].end(), value) - tree[node].begin())<<'\n';
		return (right-left+1) - (upper_bound(tree[node].begin(), tree[node].end(), value) - tree[node].begin());
	}
	return query(node*2,left,(left+right)/2,first,second,value) + query(node*2+1,(left+right)/2+1,right,first,second,value);
}

