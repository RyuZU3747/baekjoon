#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dfsnum[100011];
int cnt;
vector<int> graph[100011];
vector<pii> danjeol;

int dfs(int cur, int par){
    dfsnum[cur] = ++cnt;
    int ret = dfsnum[cur];
    for(auto nxt: graph[cur]){
        if(nxt==par) continue;
        if(dfsnum[nxt]!=0){
            ret = min(ret, dfsnum[nxt]);
            continue;
        }
        int mnchild = dfs(nxt, cur);
        if(mnchild > dfsnum[cur]){
            danjeol.push_back({min(cur,nxt),max(cur,nxt)});
        }
        ret = min(ret, mnchild);
    }
    return ret;
}

int main(){
    FASTIO;
    int v,e;cin>>v>>e;
    for(int i=0;i<e;i++){
        int a, b;cin>>a>>b;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }
    for(int i=1;i<=v;i++){
        if(dfsnum[i]==0) dfs(i, -1);
    }
    sort(danjeol.begin(),danjeol.end(),[](pii a, pii b){return a.first == b.first ? a.second < b.second : a.first < b.first;});
    cout<<danjeol.size()<<'\n';
    for(auto e: danjeol) cout<<e.first<<' '<<e.second<<'\n';
}