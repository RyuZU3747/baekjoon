using namespace std;

int dp[1010][1010]; //i~j일때 최댓값???

int main(){
	int n;cin>>n;
	vector<int> arr(n+1);
	for(int i=1;i<=n;i++) cin>>arr[i];

	for(int sz=1;sz<n;sz++){
		for(int i=1;i+sz<=n;i++){
			if(arr[i]==arr[i+sz]) dp[i][i+sz] = dp[i+1][i+sz-1] + 1;
			else dp[i][i+sz] = dp[i+1][i+sz-1];
			for(int j=i;j<=i+sz;j++){
				dp[i][i+sz] = max(dp[i][i+sz], dp[i][j] + dp[j+1][i+sz]);
			}
		}
	}

	cout<<dp[1][n];
}