#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    vector<pair<ll,ll>> cows(n);
    vector<pair<ll,ll>> store(m);
    multiset<ll> cur;
    for(int i=0;i<n;i++){
        ll a, b;cin>>a>>b;
        cows[i] = {a,b};
    }
    for(int i=0;i<m;i++){
        ll a,b;cin>>a>>b;
        store[i] = {a,b};
    }
    sort(cows.begin(), cows.end(), [](pair<ll,ll> a, pair<ll,ll> b){
        if(a.second == b.second){
            return a.first > b.first;
        }
        return a.second > b.second;
    });
    sort(store.begin(), store.end(), [](pair<ll,ll> a, pair<ll,ll> b){
        return a.second > b.second;
    });

    ll ans = 0;
    int j = 0;
    for(int i=0;i<n;i++){
        while(j < m && cows[i].second <= store[j].second) cur.insert(store[j++].first);

        auto cost = cur.lower_bound(cows[i].first);
        if(cost == cur.end()){
            cout<<-1;
            return 0;
        }
        ans += *cost;
        cur.erase(cost);
    }    
    cout<<ans;
}