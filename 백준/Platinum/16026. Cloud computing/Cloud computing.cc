#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    int n;cin>>n;
    vector<array<ll,3>> order;
    ll sumcore = 0;
    for(int i=0;i<n;i++){
        ll a,b,c;cin>>a>>b>>c;
        order.push_back({a,b,-c});
        sumcore += a;
    }
    int m;cin>>m;
    for(int i=0;i<m;i++){
        int a,b,c;cin>>a>>b>>c;
        order.push_back({-a,b,c});
    }
    sort(order.begin(),order.end(),[](array<ll,3> a, array<ll,3> b){
        if(a[1]==b[1]) return a[2]<b[2];
        return a[1] > b[1];
    });
    vector<ll> dp(sumcore+1, -1234567890123456789);
    dp[0] = 0;
    for(auto [core,clock,prize]: order){
        vector<ll> mxdp = dp;
        for(int co=0;co<=sumcore;co++){
            int bef = co - core;
            if(bef >= 0 && bef <= sumcore && dp[bef]!=-1234567890123456789){
                mxdp[co] = max(mxdp[co], dp[bef] + prize);
            }
        }
        dp = mxdp;
    }
    ll ans = -1234567890123456789;
    for(auto e: dp) ans = max(ans, e);
    cout<<ans;

}