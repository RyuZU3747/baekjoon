#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int cnt[1000001];

int main(){
	FASTIO;
	int n;cin>>n;
	vector<int> arr(n);
	int sqrtN = sqrt(n);
	for(auto &x: arr) cin>>x;
	int m;cin>>m;
	vector<array<int,3>> qry;
	for(int i=0;i<m;i++){
		int a,b;cin>>a>>b;
		qry.push_back({a-1,b-1,i});
	}
	sort(qry.begin(), qry.end(), [&sqrtN](array<int,3> a, array<int,3> b){
		if(a[0]/sqrtN == b[0]/sqrtN) return a[1] < b[1];
		return a[0]/sqrtN < b[0]/sqrtN;
	});
	vector<int> ans(m);
	int cur = 0;

	for(int i=qry[0][0];i<=qry[0][1];i++){
		if(cnt[arr[i]]==0){
			cur++;
		}
		cnt[arr[i]]++;
	}
	ans[qry[0][2]] = cur;
	int left = qry[0][0], right = qry[0][1];
	for(int i=1;i<m;i++){
		auto [s,e,idx] = qry[i];
		while(left > s){
			if(cnt[arr[--left]]==0){
				cur++;
			}
			cnt[arr[left]]++;
		}
		while(left < s){
			if(cnt[arr[left]]==1){
				cur--;
			}
			cnt[arr[left++]]--;
		}
		
		while(right > e){
			if(cnt[arr[right]]==1){
				cur--;
			}
			cnt[arr[right--]]--;
		}
		while(right < e){
			if(cnt[arr[++right]]==0){
				cur++;
			}
			cnt[arr[right]]++;
		}
		ans[idx] = cur;
	}
	for(auto e: ans) cout<<e<<'\n';
}