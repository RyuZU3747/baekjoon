int getkmp(string s, string t, int cnt){
    int fail[1001];
    memset(fail, 0, sizeof fail);
    int j = 0;
    for(int i=1;i<t.length();i++){
        while(j>0&&t[i]!=t[j]) j = fail[j-1];
        if(t[i]==t[j]) fail[i]=++j;
    }
    int ans = 0;
    for(int i=0, j=0;i<s.length();i++){
        while(j>0&&s[i]!=t[j]) j = fail[j-1];
        if(s[i]==t[j]){
            if(j==t.length()-1){
                ans++;
                j = fail[j];
            }
            else j++;
        }
    }
    if(ans-1==cnt) return 1;
    else return 0;
}

int main(){
    int n;cin>>n;
    vector<int> ord;
    vector<string> strs;
    for(int i=0;i<n;i++){
        ord.push_back(i);
        string s;cin>>s;
        strs.push_back(s);
    }
    int k;cin>>k;
    ll ans = 0;
    do{
        string curstr="";
        for(auto e:ord){
            curstr += strs[e];
        }
        ans += getkmp(curstr+curstr, curstr, k);
    }while(next_permutation(ord.begin(),ord.end()));
    cout<<ans;
}