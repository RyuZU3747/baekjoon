#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<pii> graph[100001];
int dist1[100001];
int distn[100001];
vector<pii> path[100001];
vector<array<int,3>> allpath;
int main(){
	FASTIO;
	int n,m;cin>>n>>m;
	for(int i=0;i<m;i++){
		int a,b,c;cin>>a>>b>>c;
		allpath.push_back({min(a,b), max(a,b), c});
		graph[a].push_back({b,c});
		graph[b].push_back({a,c});
	}
	memset(dist1, 0x3f, sizeof dist1);
	memset(distn, 0x3f, sizeof distn);
	priority_queue<pii> pq;
	pq.push({0,1});
	dist1[1] = 0;
	while(!pq.empty()){
		auto [cos,cur] = pq.top();
		pq.pop();
		cos = -cos;
		if(cos > dist1[cur]) continue;
		for(auto [nxt,w]:graph[cur]){
			if(dist1[nxt] > cos+w){
				dist1[nxt] = cos+w;
				path[nxt].clear();
				path[nxt].push_back({min(cur,nxt), max(cur,nxt)});
				pq.push({-dist1[nxt], nxt});
			}
			else if(dist1[nxt] == cos+w) path[nxt].push_back({min(cur,nxt), max(cur,nxt)});
		}
	}
	pq.push({0,n});
	distn[n] = 0;
	while(!pq.empty()){
		auto [cos,cur] = pq.top();
		pq.pop();
		cos = -cos;
		if(cos > distn[cur]) continue;
		for(auto [nxt,w]:graph[cur]){
			if(distn[nxt] > cos+w){
				distn[nxt] = cos+w;
				pq.push({-distn[nxt], nxt});
			}
		}
	}
	if(dist1[n] > 123456789){
		cout<<-1;
		return 0;
	}
	set<pii> paths;
	int cur = n;
	while(cur != 1){
		paths.insert(path[cur][0]);
		auto [from,to] = path[cur][0];
		if(from == cur) cur = to;
		else cur = from;
	}
	int ans = dist1[n];
	int mn = 1234567890;
	for(auto e: allpath){
		if(paths.count({e[0],e[1]})) continue;
		mn = min({mn, e[2]+dist1[e[0]]+distn[e[1]], e[2]+distn[e[1]]+dist1[e[0]]});
	}
	if(mn==1234567890) cout<<-1;
	else cout<<ans+mn;
}