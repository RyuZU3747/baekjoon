#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll dp[10000001];
ll ans = 0;
ll a,b,k;
int vis[10000001];

ll fastpow(ll a, ll x){
    ll ret = 1;
    while(x){
        if(x%2){
            ret = ret*a;
        }
        a =  a * a;
        x /= 2;
    }
    return ret;
}

ll cal(ll su){
    ll pows = 0;
    while(su){
        pows += fastpow(su%10, k);
        su /= 10;
    }
    return pows;
}


ll recur(ll cur){
    ll & ret = dp[cur];
    if(vis[cur] == 1) return min(cur,ret);
    vis[cur] = 1;
    ret = min(cur, recur(cal(cur)));
    return ret;
}

void precom(ll cur){
    if(dp[cur]) return;
    dp[cur] = cal(cur);
    precom(cal(cur));
}

int main(){
    FASTIO;
    cin>>a>>b>>k;
    for(int i=1;i<=1000000;i++){
        precom(i);
    }
    for(int i=a;i<=b;i++){
        recur(i);
    }
    memset(vis, 0, sizeof vis);
    for(int i=a;i<=b;i++){
        ans += recur(i);
    }
    cout<<ans;
}