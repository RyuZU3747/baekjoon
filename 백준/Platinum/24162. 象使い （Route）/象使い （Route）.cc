#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;



int main(){
	FASTIO;
	ll n,m;cin>>n>>m;
	vector<pll> point(n);
	vector<pll> graph[101];
	for(auto &x:point) cin>>x.first>>x.second;
	for(int i=0;i<m;i++){
		int a,b,c;cin>>a>>b>>c;
		graph[a].push_back({b,c});
		graph[b].push_back({a,c});
	}
	priority_queue<array<ll,3>> pq;
	pq.push({0,1,0});
	ll dist[101][101];
	for(int i=0;i<=n;i++){
		for(int j=0;j<=n;j++) dist[i][j] = 1234567890;
	}
	dist[1][0] = 0;
	while(!pq.empty()){
		auto [cos, cur, bef] = pq.top();
		pq.pop();
		cos = -cos;
		if(dist[cur][bef] < cos) continue;
		for(auto [nxt, w] : graph[cur]){
			if(bef != 0){
				pii v1 = {point[bef-1].first - point[cur-1].first, point[bef-1].second - point[cur-1].second};
				pii v2 = {point[nxt-1].first - point[cur-1].first, point[nxt-1].second - point[cur-1].second};
				int ang = max(0, v1.first * v2.first + v1.second * v2.second);
				// cout<<bef<<' '<<cur<<' '<<nxt<<' '<<ang<<'\n';
				if(ang) continue;
			}
			if(dist[nxt][bef] > cos + w){
				dist[nxt][bef] = cos + w;
				pq.push({-dist[nxt][bef], nxt, cur});
			}
		}
	}
	ll ans = 1234567890;
	for(int i=0;i<=n;i++){
		ans = min(ans, dist[2][i]);
	}
	cout<<(ans == 1234567890 ? -1 : ans);
}