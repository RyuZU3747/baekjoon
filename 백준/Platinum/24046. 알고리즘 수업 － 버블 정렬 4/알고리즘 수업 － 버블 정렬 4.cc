#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,k;cin>>n>>k;
    vector<int> arr(n);
    for(auto &x:arr) cin>>x;
    vector<int> idx(n-1);
    iota(idx.begin(), idx.end(), 0);
    vector<int> tmp;
    int cnt = 0;
    pii ans;
    while(idx.size() > 0){
        for(auto e: idx){
            if(arr[e] > arr[e+1]){
                swap(arr[e], arr[e+1]);
                cnt++;
                ans = {arr[e], arr[e+1]};
                if(cnt == k) break;
                if(e!=0) tmp.push_back(e-1);
            }
        }
        if(cnt == k) break;
        // for(auto e: tmp) cout<<e<<' ';
        // cout<<'\n';
        idx = tmp;
        tmp.clear();
    }
    if(cnt < k) cout<<-1;
    else cout<<ans.first<<' '<<ans.second;
}