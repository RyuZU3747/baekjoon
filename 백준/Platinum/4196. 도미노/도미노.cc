#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;


int dfsnum[100011];
int fin[100011];
vector<vector<int>> SCCs;
int cnt;
stack<int> st;
vector<int> graph[100011];

int dfs(int cur){//도달할 수 있는 최대 조상을 리턴
    dfsnum[cur] = ++cnt;
    st.push(cur);

    int ret = dfsnum[cur];
    for(auto nxt: graph[cur]){
        if(dfsnum[nxt]==0) ret = min(ret, dfs(nxt));
        else if(fin[nxt]==0) ret = min(ret, dfsnum[nxt]);
    }
    if(ret == dfsnum[cur]){
        vector<int> tmp;
        while(1){
            int t = st.top();
            st.pop();
            tmp.push_back(t);
            fin[t] = 1;
            if(t==cur) break;
        }
        sort(tmp.begin(),tmp.end());
        SCCs.push_back(tmp);
    }
    return ret;
}

int vis[100001];

void ddfs(int cur){
    if(vis[cur]) return;
    vis[cur] = 1;
    for(auto nxt: graph[cur]){
        ddfs(nxt);
    }
}

int main(){
    FASTIO;
    int _;cin>>_;
    while(_--){
        int v, e;cin>>v>>e;
        cnt = 0;
        for(int i=0;i<=100000;i++){
            dfsnum[i] = fin[i] = vis[i] = 0;
            graph[i].clear();
        }
        SCCs = vector<vector<int>>();
        while(!st.empty()) st.pop();

        for(int i=0;i<e;i++){
            int a,b;cin>>a>>b;
            graph[a].push_back(b);
        }
        for(int i=1;i<=v;i++) if(dfsnum[i]==0) dfs(i);
        reverse(SCCs.begin(),SCCs.end());
        int ans = 0;
        for(auto scc:SCCs){
            if(vis[scc[0]]==0){
                ans++;
                ddfs(scc[0]);
            }
        }
        cout<<ans<<'\n';
    }
}