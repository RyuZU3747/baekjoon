#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<ll> psum;
ll dp[801][8001];

ll cost(int l, int r){
	ll ret = (psum[r] - psum[l-1]) * (r-l+1);
	return ret;
}

void dnc(int t, int s, int e, int l, int r){
	if(s > e) return;
	int mid = s + e >> 1;
	int opt = l;
	for(int i=l;i<=r;i++){
		if(dp[t-1][opt] + cost(opt+1,mid) > dp[t-1][i] + cost(i+1,mid)) opt = i;
	}
	dp[t][mid] = dp[t-1][opt] + cost(opt+1, mid);
	dnc(t, s, mid-1, l, opt);
	dnc(t, mid+1, e, opt, r);
}

int main(){
	FASTIO;
	int l,g;cin>>l>>g;
	vector<ll> arr(l);
	psum.resize(l+1);
	for(auto &x: arr) cin>>x;
	for(int i=1;i<=l;i++) psum[i] = psum[i-1] + arr[i-1];

	for(int i=0;i<=l;i++) dp[1][i] = cost(1,i);
	for(int i=2;i<=g;i++) dnc(i, 0, l, 0, l);
	cout<<dp[g][l];	
}