#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
	ll n, d;cin>>n>>d;
	vector<ll> cord(n);
	for(auto &x: cord) cin>>x;
	ll ans = 0;
	sort(cord.begin(), cord.end());
	if(cord.back() <= 0){
		cout<<abs(cord[0]) * 2 + d;
        return 0;
	}
	else if(cord[0] >= 0){
		cout<<abs(cord.back()) * 2 + d;
        return 0;
	}
	else{
		ans = (abs(cord[0]) + abs(cord.back())) * 2 + 2*d;
        ans = min(ans, abs(cord[0]) * 4 + abs(cord.back()) * 2 + d);
        ans = min(ans, abs(cord[0]) * 2 + abs(cord.back()) * 4 + d);
        if((abs(cord[0]) + abs(cord.back())) * 2 >= d) ans = min(ans, abs(cord[0]) * 4 + abs(cord.back()) * 4);
        else ans = min(ans, abs(cord[0]) * 2 + abs(cord.back()) * 2 + d);
	}
	cout<<ans<<'\n';
}