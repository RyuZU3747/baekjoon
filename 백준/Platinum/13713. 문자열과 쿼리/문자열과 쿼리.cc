#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<int> z(1000001);

void calz(string s){
    int n = s.length();
    int l = 0,r = 0,t;
    for(int i=1;i<n;i++){
        if(i>r){
            l = r = i;
            while(r<n&&s[r-l]==s[r]) r++;
            z[i] = r-l;
            r--;
        }
        else{
            t=i-l;
            if(z[t]<r-i+1) z[i] = z[t];
            else{
                l = i;
                while(r<n&&s[r-l]==s[r]) r++;
                z[i] = r-l;
                r--;
            }
        }
    }
}

int main(){
    FASTIO;
    string s;cin>>s;
    reverse(s.begin(),s.end());
    calz(s);
    int m;cin>>m;
    reverse(z.begin(), z.begin() + s.length());
    z[s.length()-1] = s.length();
    for(int i=0;i<m;i++){
        int a;cin>>a;
        a--;
        cout<<z[a]<<'\n';
    }
}