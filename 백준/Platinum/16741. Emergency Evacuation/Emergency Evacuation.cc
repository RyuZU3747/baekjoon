#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int r, s, p;cin>>r>>s>>p;
    int ed = r + 1;
    vector<int> dis;
    for(int i=0;i<p;i++){
        int x,y;cin>>y>>x;
        int pa = ed - y;
        int sit;
        if(x <= s) sit = s-x+1;
        else sit = x - s;
        dis.push_back(pa+sit);
    }
    sort(dis.begin(),dis.end());
    for(int i=1;i<p;i++){
        dis[i] = max(dis[i-1]+1, dis[i]);
    }
    cout<<dis[p-1];
}