#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dfsnum[5011];
int fin[5011];
vector<vector<int>> SCCs;
int cnt;
stack<int> st;
vector<int> graph[5011];
int sccnum[5011];
int sccoutdegree[5011];
int scccnt;

int dfs(int cur){//도달할 수 있는 최대 조상을 리턴
    dfsnum[cur] = ++cnt;
    st.push(cur);

    int ret = dfsnum[cur];
    for(auto nxt: graph[cur]){
        if(dfsnum[nxt]==0) ret = min(ret, dfs(nxt));
        else if(fin[nxt]==0) ret = min(ret, dfsnum[nxt]);
    }
    if(ret == dfsnum[cur]){
        vector<int> tmp;
        while(1){
            int t = st.top();
            st.pop();
            sccnum[t] = scccnt;
            tmp.push_back(t);
            fin[t] = 1;
            if(t==cur) break;
        }
        sort(tmp.begin(),tmp.end());
        SCCs.push_back(tmp);
        scccnt++;
    }
    return ret;
}

int main(){
    FASTIO;
    while(1){
        memset(dfsnum, 0, sizeof dfsnum);
        memset(fin, 0, sizeof fin);
        memset(sccnum, 0, sizeof sccnum);
        memset(sccoutdegree, 0, sizeof sccoutdegree);
        SCCs.clear();
        cnt = 0;
        scccnt = 0;
        for(int i=0;i<5011;i++) graph[i].clear();
        while(!st.empty()) st.pop();
        
        int n;cin>>n;
        if(n==0) break;
        int m;cin>>m;
        for(int i=0;i<m;i++){
            int a,b;cin>>a>>b;
            graph[a].push_back(b);
        }
        for(int i=1;i<=n;i++) if(dfsnum[i]==0) dfs(i);
        for(int i=1;i<=n;i++){
            for(auto e:graph[i]){
                if(sccnum[i] != sccnum[e]) sccoutdegree[sccnum[i]]++;
            }
        }
        vector<int> ans;
        for(int i=0;i<scccnt;i++){
            if(sccoutdegree[i]==0){
                for(auto e: SCCs[i]) ans.push_back(e);
            }
        }
        sort(ans.begin(),ans.end());
        for(auto e: ans) cout<<e<<' ';
        cout<<'\n';
    }
}