#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dist[5010];
int r[5010];
int c[5010];
vector<int> g[5010];
vector<int> graph[5010];

int main(){
    int n,k;cin>>n>>k;
    for(int i=1;i<=n;i++){
        int a,b;cin>>a>>b;
        c[i] = a;
        r[i] = b;
    }
    for(int i=0;i<k;i++){
        int a,b;cin>>a>>b;
        g[a].push_back(b);
        g[b].push_back(a);
    }
    for(int i=1;i<=n;i++){
        vector<int> v(n+1,0);
        queue<pii> q;
        q.push({i,0});
        v[i] = 1;
        int ran = r[i];
        while(!q.empty()){
            int cur = q.front().first;
            int d = q.front().second;
            q.pop();
            if(i!=cur) graph[i].push_back(cur);
            if(d==ran) continue;
            for(auto nxt: g[cur]){
                if(v[nxt]) continue;
                v[nxt] = 1;
                q.push({nxt, d+1});
            }
        }
    }

    for(int i=1;i<=n;i++) dist[i] = 1234567890;
    priority_queue<pii> pq;
    pq.push({0,1});
    dist[1] = 0;
    while(!pq.empty()){
        int cost = -pq.top().first;
        int cur = pq.top().second;
        pq.pop();
        if(cost > dist[cur]) continue;
        for(auto nxt : graph[cur]){
            int nc = cost + c[cur];
            if(dist[nxt] > nc){
                dist[nxt] = nc;
                pq.push({-nc, nxt});
            }
        }
    }
    cout<<dist[n];
}