#include <bits/stdc++.h>
#define ll long long int
#define MOD 5000000
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll tree[51][100001];
vector<int> arr(100001);

ll sum(int k, int idx){
    if(k==0) return 1;
    ll ret = 0;
    while(idx > 0){
        ret += tree[k][idx];
        ret %= MOD;
        idx -= (idx & -idx);
    }
    return ret;
}

void update(int k, int idx, ll val){
    while(idx < 100001){
        tree[k][idx] += val;
        tree[k][idx] %= MOD;
        idx += (idx & -idx);
    }
}

int main(){
    FASTIO;
    int n,k;cin>>n>>k;
    for(int i=0;i<n;i++) cin>>arr[i];
    for(int i=0;i<n;i++){
        for(int j=1;j<=k;j++){
            update(j, arr[i], sum(j-1, arr[i]-1));
        }
    }
    cout<<sum(k, 100000);
}