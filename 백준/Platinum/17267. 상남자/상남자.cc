#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int vis[1001][1001];
int dr[4] = {1,-1,0,0};
int dc[4] = {0,0,-1,1};

int main(){
    int n,m;cin>>n>>m;
    int l,r;cin>>l>>r;
    string mp[1001];
    pii st;
    for(int i=0;i<n;i++){
        cin>>mp[i];
        for(int j=0;j<m;j++){
            if(mp[i][j]=='2') st = {i,j};
        }
    }
    queue<array<int,4>> q;//r,c,l,r
    q.push({st.first,st.second,l,r});
    vis[st.first][st.second] = 1;
    while(!q.empty()){
        auto [ro,co,cl,cr] = q.front();
        q.pop();
        int tr = ro+dr[0], tc = co+dc[0];
        while(!(tr<0||tr>=n||tc<0||tc>=m||vis[tr][tc]==1||mp[tr][tc]=='1')){
            q.push({tr,tc,cl,cr});
            vis[tr][tc] = 1;
            tr += dr[0];
            tc += dc[0];
        }
        tr = ro+dr[1];
        tc = co+dc[1];
        while(!(tr<0||tr>=n||tc<0||tc>=m||vis[tr][tc]==1||mp[tr][tc]=='1')){
            q.push({tr,tc,cl,cr});
            vis[tr][tc] = 1;
            tc += dc[1];
            tr += dr[1];
        }
        for(int i=2;i<4;i++){
            int nro = ro + dr[i];
            int nco = co + dc[i];
            if(nro<0||nro>=n||nco<0||nco>=m||vis[nro][nco]==1||mp[nro][nco]=='1') continue;
            if(i==2&&cl>0){
                q.push({nro,nco,cl-1,cr});
                vis[nro][nco] = 1;
            }
            if(i==3&&cr>0){
                q.push({nro,nco,cl,cr-1});
                vis[nro][nco] = 1;
            }
        }
    }
    int ans = 0;
    for(int i=0;i<n;i++){
        for(int j=0;j<m;j++){
            if(vis[i][j]==1) ans++;
        }
    }
    cout<<ans;
}