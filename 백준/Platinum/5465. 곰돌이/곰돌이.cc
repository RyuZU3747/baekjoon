#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dr[4] = {0,0,1,-1};
int dc[4] = {1,-1,0,0};
int bee[801][801];
string mp[801];
pii home;
pii bear;
int n,s;
int vis[801][801];

int check(int val){
    memset(vis, 0, sizeof vis);
    if(bee[bear.first][bear.second] <= val) return 0;
    queue<array<int,3>> q;  
    q.push({bear.first, bear.second, 0});
    vis[bear.first][bear.second] = 1;
    while(!q.empty()){
        auto [cr,cc,cnt] = q.front();
        q.pop();
        for(int i=0;i<4;i++){
            int nr = cr+dr[i];
            int nc = cc+dc[i];
            if(nr==home.first&&nc==home.second) return 1;
            if(nr<0||nr>=n||nc<0||nr>=n||vis[nr][nc]==1) continue;
            if(bee[nr][nc] <= val+(cnt+1)/s) continue;
            q.push({nr,nc,cnt+1});
            vis[nr][nc] = 1;
        }
    }
    return 0;
}

int main(){
    cin>>n>>s;
    queue<array<int,3>> q;
    memset(bee, -1, sizeof bee);
    for(int i=0;i<n;i++){
        cin>>mp[i];
        for(int j=0;j<n;j++){
            if(mp[i][j] == 'M') bear = {i,j};
            if(mp[i][j] == 'D') home = {i,j};
            if(mp[i][j] == 'H'){
                q.push({i,j,0});
                bee[i][j] = 0;
            }
        }
    }
    while(!q.empty()){
        auto [cr,cc,cnt] = q.front();
        q.pop();
        for(int i=0;i<4;i++){
            int nr = cr+dr[i];
            int nc = cc+dc[i];
            if(nr<0||nr>=n||nc<0||nr>=n||bee[nr][nc]!=-1) continue;
            if(mp[nr][nc]!='G'&&mp[nr][nc]!='M') continue;
            q.push({nr,nc,cnt+1});
            bee[nr][nc] = cnt+1;
        }
    }
    int left = 0, right = 100000000;
    int ans = -1;
    while(left<=right){
        int mid = (left+right)/2;
        if(check(mid)){
            ans = max(ans, mid);
            left = mid + 1;
        }
        else right = mid - 1;
    }
    cout<<ans;
}