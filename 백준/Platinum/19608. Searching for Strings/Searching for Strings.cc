#include <bits/stdc++.h>
#define ll long long int
#define MOD1 1000000007
#define MOD2 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll fastpow(ll a, ll x, ll m){
    ll ret = 1;
    while(x){
        if(x%2){
            ret = ret*a%m;
        }
        a =  a * a%m;
        x /= 2;
    }
    return ret%m;
}

int main(){
    FASTIO;
    string n,h;cin>>n>>h;
    int alp[27], heyalp[27];
    memset(alp, 0, sizeof alp);
    memset(heyalp, 0, sizeof heyalp);
    
    int len = n.length();
    if(h.length() < len){
        cout<<0;
        return 0;
    }
    
    for(int i=0;i<len;i++){
        alp[n[i]-'a']++;
    }

    ll rol1 = 0;
    ll rol2 = 0;
    set<ll> hash;
    ll mul = 31;
    ll ans = 0;
    for(int i=0;i<=h.length()-len;i++){
        if(i==0){
            for(int j=0;j<len;j++){
                rol1 = (rol1 * mul + h[j]) % MOD1;
                rol2 = (rol2 * mul + h[j]) % MOD2;
                heyalp[h[j]-'a']++;
            }
        }
        else{
            rol1 = rol1 - (h[i-1] * fastpow(mul, len-1, MOD1)) % MOD1 + MOD1;
            rol1 %= MOD1;
            rol2 = rol2 - (h[i-1] * fastpow(mul, len-1, MOD2)) % MOD2 + MOD2;
            rol2 %= MOD2;
            heyalp[h[i-1]-'a']--;
            rol1 = (rol1 * mul + h[len+i-1]) % MOD1;
            rol2 = (rol2 * mul + h[len+i-1]) % MOD2;
            heyalp[h[len+i-1]-'a']++;
        }
        int no = 0;
        for(int j=0;j<27;j++){
            if(alp[j]!=heyalp[j]) no = 1;
        }
        ll t = (rol1<<32)|rol2;
        if(no == 0 && hash.find(t)==hash.end()){
            hash.insert(t);
            ans++;
        }
    }
    cout<<ans;
}