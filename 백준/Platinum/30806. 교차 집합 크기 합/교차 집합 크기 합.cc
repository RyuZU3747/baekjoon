#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll fact[1000001];

ll fastpow(ll a, ll x, ll m){
    ll ret = 1;
    while(x){
        if(x%2){
            ret = ret*a%m;
        }
        a =  a * a%m;
        x /= 2;
    }
    return ret;
}

int main(){
    FASTIO;
    int n;cin>>n;
    fact[0] = 1;
    fact[1] = 1;
    for(int i=2;i<=n;i++){
        fact[i] = (fact[i-1] * i) % MOD;
    }
    map<ll,ll> cnt;
    ll ans[n+1]= {0,};
    for(int i=0;i<n;i++){
        int a;cin>>a;
        while(a--){
            int b;cin>>b;
            cnt[b]++;
        }
    }
    for(auto e: cnt){
        for(ll i=1;i<=e.second;i++){
            ans[i] += (fact[e.second] * fastpow((fact[i] * fact[e.second - i]) % MOD, MOD-2, MOD)) % MOD;
            ans[i] %= MOD;
        }
    }
    for(int i=1;i<=n;i++) cout<<ans[i]<<'\n';
}