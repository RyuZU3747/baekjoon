#include <bits/stdc++.h>
#define ll long long int
#define swap(a,b) {ll c; c=a;a=b;b=c;}
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    ll n;cin>>n;
    vector<pair<ll, char>> cows(n);
    for(int i=0;i<n;i++){
		cin>>cows[i].first>>cows[i].second;
	}
    sort(cows.begin(), cows.end());
    ll ans = 0;
	vector<pii> od;
	vector<pii> ev;
	int sum = 0;
	for(int i=0;i<n;i++){
		if(sum&1){
			if(od.empty() || od.back().first < sum) od.push_back({sum, cows[i].first});
		}
		else{
			if(ev.empty() || ev.back().first < sum) ev.push_back({sum, cows[i].first});
		}

		if(cows[i].second == 'W') sum += -1;
		else sum += 1;

		if(sum&1){
			if(!od.empty() && sum <= od.back().first){
				int fd = lower_bound(od.begin(), od.end(), make_pair(sum, -1234567890))->second;
				ans = max(ans, cows[i].first - fd);
			}
		}
		else{
			if(!ev.empty() && sum <= ev.back().first){
				int fd = lower_bound(ev.begin(), ev.end(), make_pair(sum, -1234567890))->second;
				ans = max(ans, cows[i].first - fd);
			}
		}
	}
    cout<<ans;
}