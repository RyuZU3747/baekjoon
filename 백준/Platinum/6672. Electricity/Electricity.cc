#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dfsnum[100011];
int cnt;
vector<int> graph[100011];
stack<pii> st;
vector<vector<pii>> bcc;
int nodebcc[100011];

int dfs(int cur, int par){
    dfsnum[cur] = ++cnt;
    int ret = dfsnum[cur];
    for(auto nxt: graph[cur]){
        if(nxt==par) continue;
        if(dfsnum[cur] > dfsnum[nxt]) st.push({cur,nxt});
        if(dfsnum[nxt]!=0){
            ret = min(ret, dfsnum[nxt]);
            continue;
        }

        int mnchild = dfs(nxt, cur);
        if(mnchild >= dfsnum[cur]){
            nodebcc[cur]++;
            vector<pii> tmp;
            while(!st.empty()&&st.top()!=make_pair(cur,nxt)){
                tmp.push_back(st.top());
                st.pop();
            }
            tmp.push_back(st.top());
            st.pop();
            bcc.push_back(tmp);
        }
        ret = min(ret, mnchild);
    }
    return ret;
}

int main(){
    FASTIO;
    while(1){
        int v,e;cin>>v>>e;
        if(v==0) break;
        for(int i=0;i<v;i++){
            dfsnum[i] = 0;
            nodebcc[i] = 1;
            graph[i].clear();
        }
        cnt = 0;
        bcc.clear();
        while(!st.empty()) st.pop();
        for(int i=0;i<e;i++){
            int a, b;cin>>a>>b;
            graph[a].push_back(b);
            graph[b].push_back(a);
        }
        int ans = 0;
        for(int i=0;i<v;i++){
            if(dfsnum[i]==0){
                dfs(i, -1);
                ans++;
                nodebcc[i]--;
            }
        }
        int x = 0;
        for(int i=0;i<v;i++) x = max(x, nodebcc[i]);
        cout<<ans+x-1<<'\n';
    }
}