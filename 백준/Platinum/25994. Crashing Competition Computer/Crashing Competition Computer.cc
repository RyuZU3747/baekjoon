#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000003
#define pii pair<int, int>
#define pll pair<ll, ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

double dp[2002];
double ans[2002];
double t,r,p;
int c;


int main(){
    FASTIO;
    cin>>c>>t>>r>>p;
    dp[0] = 0;
    for(int i=1;i<=c;i++){
        dp[i] = (dp[i-1] + 1 + p * r) / (1-p);
    }
    for(int i=1;i<=c;i++){
        ans[i] = dp[i];
        for(int j=0;j<=i;j++) ans[i] = min(ans[i], ans[j] + dp[i-j] + t);
    }
    cout<<fixed;
    cout.precision(10);
    cout<<ans[c] + t;
}