#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int n;
pii dp[10001][10];
int current[10001];
int make[10001];

int recur(int idx, int turn){//횟수 리턴
    if(idx==n) return 0;
    int curnum = (current[idx] + turn) % 10;
    pii & ret = dp[idx][curnum];
    if(ret.first != -1) return ret.first;
    int right = (curnum - make[idx] + 10) % 10;
    int left = (make[idx] - curnum + 10) % 10;

    int leftt = recur(idx+1, turn + left) + left;
    int rightt = recur(idx+1, turn) + right;
    if(leftt<rightt){
        ret.first = leftt;
        ret.second = left;
    }
    else{
        ret.first = rightt;
        ret.second = -right;
    }
    return ret.first;
}

int main(){
    scanf("%d",&n);
    for(int i=0;i<n;i++) scanf("%1d",&current[i]);
    for(int i=0;i<n;i++) scanf("%1d",&make[i]);
    for(int i=0;i<n;i++){
        for(int j=0;j<10;j++) dp[i][j].first = -1;
    }

    int ans = recur(0,0);
    printf("%d\n",ans);

    int curnum = current[0];
    int turn = 0;
    for(int i=0;i<n;i++){
        if(dp[i][curnum].second != 0) printf("%d %d\n",i+1, dp[i][curnum].second);
        if(dp[i][curnum].second > 0) turn = (turn + dp[i][curnum].second) % 10;
        curnum = (current[i+1] + turn) % 10;
    }
}