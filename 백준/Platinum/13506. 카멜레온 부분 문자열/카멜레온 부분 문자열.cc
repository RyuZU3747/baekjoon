#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<int> Z(string s){
    int n = s.length();
    vector<int> Zarr(n);
    int L = 0, R = 0;
    for(int i=1;i<n;i++){
        if(i + Zarr[i-L] < R) Zarr[i] = Zarr[i-L];
        else{
            L = i, R = max(R, i);
            while(R<n && s[R] == s[R-i]) ++R;
            Zarr[i] = R-i;
        }
    }
    return Zarr;
}

int main(){
    FASTIO;
    string s;cin>>s;
    vector<int> zarr = Z(s);
    vector<int> mx(s.length());
    int t = -1;
    for(int i=0;i<s.length();i++){
        mx[i] = t;
        t = max(t, zarr[i]);
    } 
    int mxlen = 0;
    for(int i=0;i<s.length();i++){
        if(zarr[i] == s.length() - i && mx[i] >= s.length() - i){
            mxlen = max(mxlen, (int)s.length() - i);
        }
    }
    if(mxlen) cout<<s.substr(0, mxlen);
    else cout<<-1;
}