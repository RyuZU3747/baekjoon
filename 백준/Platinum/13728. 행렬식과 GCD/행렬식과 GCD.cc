#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    n++;
    ll det[100001];
    ll ans = 0;
    det[1] = 1;
    det[2] = 1;
    det[3] = 2;
    det[4] = 3;
    for(int i=5;i<=n+1;i++){
        det[i] = det[i-1] + det[i-2];
        det[i] %= MOD;
    }
    for(int i=2;i<=n+1;i++){
        ans += det[gcd(n,i)];
        ans %= MOD;
    }
    cout<<ans-1;
}