#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int dfsnum[10011];
int cnt;
vector<int> graph[10011];
int danjeol[10011];

int dfs(int cur, int root){
    dfsnum[cur] = ++cnt;
    int child = 0;
    int ret = dfsnum[cur];
    for(auto nxt: graph[cur]){
        if(dfsnum[nxt]!=0){
            ret = min(ret, dfsnum[nxt]);
            continue;
        }
        child++;
        int mnchild = dfs(nxt, root);
        if(cur != root && mnchild >= dfsnum[cur]){
            danjeol[cur] = 1;
        }
        ret = min(ret, mnchild);
    }
    if(cur == root && child >= 2) danjeol[cur] = 1;
    return ret;
}

int main(){
    FASTIO;
    int v,e;cin>>v>>e;
    for(int i=0;i<e;i++){
        int a, b;cin>>a>>b;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }
    for(int i=1;i<=v;i++){
        if(dfsnum[i]==0) dfs(i, i);
    }
    int ans = 0;
    for(int i=1;i<=v;i++) if(danjeol[i]==1) ans++;
    cout<<ans<<'\n';
    for(int i=1;i<=v;i++) if(danjeol[i]==1) cout<<i<<' ';
}