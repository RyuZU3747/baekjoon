#include <bits/stdc++.h>
#define ll long long int
#define swap(a,b) {ll c; c=a;a=b;b=c;}
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define eps 1e-7
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
	int n;cin>>n;
	vector<ll> arr(n);
	vector<ll> ln(n+1);
	vector<ll> lx(n+1);
	vector<ll> rn(n+1,n+1);
	vector<ll> rx(n+1,n+1);
	vector<ll> st;
	for(auto &x:arr) cin>>x;
	for(int i=1;i<=n;i++){
		while(!st.empty() && arr[st.back()-1] >= arr[i-1]) st.pop_back();
		if(!st.empty()) ln[i] = st.back();
		st.push_back(i);
	}
	st.clear();
	for(int i=1;i<=n;i++){
		while(!st.empty() && arr[st.back()-1] <= arr[i-1]) st.pop_back();
		if(!st.empty()) lx[i] = st.back();
		st.push_back(i);
	}
	st.clear();
	for(int i=n;i>0;i--){
		while(!st.empty() && arr[st.back()-1] > arr[i-1]) st.pop_back();
		if(!st.empty()) rn[i] = st.back();
		st.push_back(i);
	}
	st.clear();
	for(int i=n;i>0;i--){
		while(!st.empty() && arr[st.back()-1] < arr[i-1]) st.pop_back();
		if(!st.empty()) rx[i] = st.back();
		st.push_back(i);
	}
	ll ans = 0;
	for(ll i=1;i<=n;i++){
		ans -= arr[i-1] * (rn[i] - i) * (i - ln[i]);
		ans += arr[i-1] * (rx[i] - i) * (i - lx[i]);
	}
	cout<<ans;
}