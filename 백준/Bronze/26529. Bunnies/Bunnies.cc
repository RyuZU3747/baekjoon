#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll dp[50];

ll recur(int n){
    if(n <= 1) return 1;
    ll & ret = dp[n];
    if(ret != -1) return ret;
    ret = recur(n-2) + recur(n-1);
    return ret;
}

int main(){
	FASTIO;
    memset(dp, -1, sizeof dp);
    int n;cin>>n;
    while(n--){
        int a;cin>>a;
        cout<<recur(a)<<'\n';
    }
}