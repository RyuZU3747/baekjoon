using namespace std;

int main() {
	FASTIO;
	int n;cin>>n;
	map<int, int> cnt;
	vector<int> arr;
	for(int i=0;i<n;i++){
		int a;cin>>a;
		arr.push_back(a);
		cnt[a]++;
	}
	int ans = -1;
	for(auto e: cnt){
		if(e.second == 1) ans = e.first;
	}
	if(ans == -1) cout<<"none";
	else{
		for(int i=0;i<n;i++){
			if(ans == arr[i]) cout<<i+1;
		}
	}
}