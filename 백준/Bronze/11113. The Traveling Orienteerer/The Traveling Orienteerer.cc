#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main() {
	FASTIO;
	int n;cin>>n;
	vector<pair<double, double>> arr(n);
	for(auto &x: arr) cin>>x.first>>x.second;
	int m;cin>>m;
	while(m--){
		int k;cin>>k;
		double ans = 0;
		vector<pair<double, double>> srt;
		for(int i=0;i<k;i++){
			int a;cin>>a;
			srt.push_back(arr[a]);
		}
		for(int i=1;i<k;i++){
			ans += hypot(abs(srt[i-1].first - srt[i].first), abs(srt[i-1].second - srt[i].second));
		}
		cout<<fixed;
		cout.precision(0);
		cout<<ans<<'\n';
	}
}