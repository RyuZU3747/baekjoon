#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
	int n;cin>>n;
	vector<int> arr(n);
	vector<int> co(n);
	pii mx={0,0};
	for(auto &x: arr){
		cin>>x;
		if(mx.first > x) mx.second = max(mx.second, x);
		else mx.second=mx.first,mx.first=x;
	}
	for(auto &x: co) cin>>x;
	for(int i=0;i<n;i++){
		if(mx.first==arr[i]) cout<<arr[i]-co[i]-(mx.second-co[i])<<' ';
		else cout<<arr[i]-co[i]-(mx.first-co[i])<<' ';
	}
}