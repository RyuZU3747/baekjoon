#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    int fh,fm,sh,sm;
    scanf("%d:%d %d:%d",&fh,&fm,&sh,&sm);
    int n,t;scanf("%d %d",&n,&t);
    int ch = fh, cm = fm;
    int day = 0;
    for(int i=0;i<=n;i++){
        cm+=t;
        while(cm>=60){
            cm -= 60;
            ch++;
        }
        if(ch>sh || (ch==sh && cm >= sm)){
            ch = fh, cm = fm;
            day++;
            i--;
        }
    }
    printf("%d\n%02d:%02d",day,ch,cm);
}