    FASTIO;
    string s;cin>>s;
    double sum = 0;
    int cnt = 0;
    for(int i=0;i<s.length();i++){
        if(s[i]=='A'){
            sum += 4.0;
            cnt ++;
        }
        if(s[i]=='B'){
            sum += 3.0;
            cnt ++;
        }
        if(s[i]=='C'){
            sum += 2.0;
            cnt ++;
        }
        if(s[i]=='D'){
            sum += 1.0;
            cnt ++;
        }
        if(s[i]=='F'){
            cnt++;
        }
        if(s[i+1]=='+'){
            sum += 0.5;
            i++;
        }
    }
    cout<<fixed;
    cout.precision(5);
    cout<<(double)sum/cnt;
