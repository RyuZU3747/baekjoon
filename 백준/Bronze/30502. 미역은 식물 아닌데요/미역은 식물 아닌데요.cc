#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

struct sik{
    int p = -1;
    int m = -1;
};

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    vector<sik> sickmul(n+1);
    for(int i=0;i<m;i++){
        int a, c;
        char b;
        cin>>a>>b>>c;
        if(b=='P') sickmul[a].p = c;
        else sickmul[a].m = c;
    }
    int mn = 0, mx = 0;
    for(int i=1;i<=n;i++){
        if(sickmul[i].p==0 || sickmul[i].m==1) continue;

        if(sickmul[i].p==1 && sickmul[i].m==0){
            mn++;mx++;
        }
        else if(sickmul[i].m==-1 || sickmul[i].p==-1){
            mx++;
        }
    }
    cout<<mn<<' '<<mx;
}