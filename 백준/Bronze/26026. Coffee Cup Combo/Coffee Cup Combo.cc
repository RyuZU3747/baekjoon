#define ll long long int
#define MOD 1000000003
#define pii pair<int, int>
#define pll pair<ll, ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    string s;cin>>s;
    int cof = 0;
    int ans = 0;
    for(int i=0;i<n;i++){
        if(s[i]=='1'){
            ans ++;
            cof = 2;
        }
        else{
            if(cof){
                cof--;
                ans++;
            }
        }
    }
    cout<<ans;
}