    int n,k;
    vector<int> arr(1);
    cin>>n>>k>>arr[0];
    vector<int> d(n);
    for(auto &x: d)cin>>x;
    for(int i=1;i<=n;i++){
        if(arr.back() < k) arr.push_back(arr[i-1] + d[i-1] + abs(arr[i-1] - k));
        else if(arr.back() > k) arr.push_back(arr[i-1] + d[i-1] - abs(arr[i-1] - k));
        else arr.push_back(arr[i-1] + d[i-1]);
    }
    int ans = 0;
    for(int i=1;i<=n;i++) ans += abs(arr[i]-k);
    cout<<ans;
