#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main() {
	FASTIO;
	int _;cin>>_;
	cin.ignore();
	while(_--){
		string s;
		getline(cin, s);
		int idx = 0;
		vector<string> ss;
		while(idx<s.length()){
			while(s[idx] == ' ' && idx<s.length()) idx++;
			string t;
			while(s[idx] != ' ' && idx<s.length()) t += s[idx++];
			ss.push_back(t);
		}
		string ans;
		for(auto e: ss){
			int sum = 0;
			for(int i=0;i<e.length();i++){
				sum += e[i]-'a';
			}
			sum %= 27;
			if(sum == 26) ans += ' ';
			else ans += sum+'a';
		}
		cout<<ans<<'\n';
	}
}