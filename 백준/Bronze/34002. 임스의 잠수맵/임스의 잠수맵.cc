#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int a,b,c;cin>>a>>b>>c;
    int s,v;cin>>s>>v;
    int l;cin>>l;
    int ned = (250-l)*100;
    int ans = 0;
    while(ned>0){
        if(v){
            v--;
            if(ned >= 30*c){
                ans += 30;
                ned -= 30*c;
            }
            else{
                ans += ceil(1.0*ned/c);
                break;
            }
        }
        else if(s){
            s--;
            if(ned >= 30*b){
                ans += 30;
                ned -= 30*b;
            }
            else{
                ans += ceil(1.0*ned/b);
                break;
            }
        }
        else{
            ans++;
            ned -= a;
        }
    }
    cout<<ans;
}