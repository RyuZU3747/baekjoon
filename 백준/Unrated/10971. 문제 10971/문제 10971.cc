#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int w[11][11];
int n;
int vis[11];
int len = 1;
int ans = 100000000;
void recur(int cur, int cos, int st){
    if(len == n && w[cur][st] != 0){
        ans = min(ans,cos + w[cur][st]);
        return;
    }

    for(int i=1;i<=n;i++){
        if(w[cur][i] != 0){
            int nxt = i;
            if(vis[nxt] == 0){
                vis[nxt] = 1;
                len += 1;
                recur(nxt, cos + w[cur][i], st);
                vis[nxt] = 0;
                len -= 1;
            }
        }
    }
}

int main(){
    FASTIO;
    cin>>n;
    for(int i=1;i<=n;i++){
        for(int j=1;j<=n;j++){
            cin>>w[i][j];
        }
    }
    for(int i=1;i<=n;i++){
        vis[i] = 1;
        recur(i, 0, i);
        vis[i] = 0;
    }
    cout<<ans;
}