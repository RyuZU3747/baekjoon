#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int y_0,m0,d0;
int y_1,m1,d1;

int ispast(int y, int bty, int m, int d){
    int ret = y - bty;
    if(y < y_1) return ret; 
    else if(y == y_1){
        if(m < m1) return ret;
        else if(m == m1){
            if(d <= d1) return ret;
        }
    }
    return 0;
}

int main(){
	FASTIO;
    cin>>y_0>>m0>>d0;
    cin>>y_1>>m1>>d1;
    int n;cin>>n;
    vector<array<int, 3>> arr(n);
    for(auto &x: arr) cin>>x[0]>>x[1]>>x[2];
    int today = y_0 - arr[0][0];
    int ans = 0;
    for(int i=2026;i<=y_1;i++){
        for(auto e: arr){
            if(i==2026){
                if(e[1] < 3) continue;
                if(e[1] == 3 && e[2] < 30) continue;
            }
            int ret = ispast(i, e[0], e[1], e[2]);
            if(ret > today){
                // cout<<i<<' '<<e[1]<<' '<<e[2]<<'\n';
                ans++;
            }
        }
    }
    cout<<ans;
}