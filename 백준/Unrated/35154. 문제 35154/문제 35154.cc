#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll dp[5050][5050];

ll recur(int n, int k){
    if(n == 0) return 1;
    if(n < 0) return 0;
    ll & ret = dp[n][k];
    if(ret != -1) return ret;
    ret = 0;
    if(k != 0) ret += recur(n-1, k-1);
    ret %= MOD;
    ret += recur(n-2, k+1);
    return ret %= MOD;
}

int main(){
	FASTIO;
    memset(dp, -1, sizeof dp);
    int t;cin>>t;
    while(t--){
        int n;cin>>n;
        int ans = recur(n,0);
        cout<<(ans==0?-1:ans)<<'\n';
    }
}