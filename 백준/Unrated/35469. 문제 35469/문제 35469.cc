#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int arr[1010][1010];

int main(){
    FASTIO;
    int n,k;cin>>n>>k;
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            cin>>arr[i][j];
        }
    }
    if(k == 1){
        cout<<"LOSE";
        return 0;
    }
    int cur = 0;
    int ad = 1;
    queue<int> q;
    for(int i=0;i<k-1;i++){
        arr[0][i] += ad;
        cur += ad;
        q.push(ad);
    }
    cur = -cur;
    for(int i=k-1;i<n;i++){
        arr[0][i] += cur;
        q.push(cur);
        cur = q.front();
        q.pop();
    }

    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            cout<<arr[i][j]<<' ';
        }
        cout<<'\n';
    }
}
