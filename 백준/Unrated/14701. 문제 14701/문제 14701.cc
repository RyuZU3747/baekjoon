#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#include <ext/pb_ds/assoc_container.hpp>
#include <ext/pb_ds/tree_policy.hpp>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;
using namespace __gnu_pbds;
#define ordered_set tree<int, null_type, less<int>, rb_tree_tag,tree_order_statistics_node_update>

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    ordered_set left, right;
    for(int i=1;i<n/2+1;i++) left.insert(i);
    if(n%2) left.insert(n/2+1);
    for(int i=n;i>n/2;i--) right.insert(-i);
    
    int f = 0;
    for(int i=0;i<m;i++){
        int a,b;cin>>a>>b;
        if(a==1){
            if(n%2 && f==0){
                if(b==n/2+1){
                    left.erase(b);
                    right.erase(-b);
                }
                else if(b < n/2+1) right.erase(-(n/2+1));
                else left.erase(n/2+1);
                f=1;
            }

            if(left.find(b)!=left.end()){
                left.erase(b);
            }
            else if(right.find(-b)!=right.end()){
                right.erase(-b);
            }
        }
        else{
            if(b<=n/2){
                auto iter = left.find_by_order(b-1);
                if(iter==left.end()){
                    cout<<0<<'\n';
                    continue;
                }
                cout<<*iter<<'\n';
            }
            else{
                b = n-b;
                auto iter = right.find_by_order(b);
                if(iter==right.end()){
                    cout<<0<<'\n';
                    continue;
                }
                cout<<-*iter<<'\n';
            }
        }
    }
}