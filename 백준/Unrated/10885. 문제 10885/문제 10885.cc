#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int, int>
#define pll pair<ll, ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll fastpow(ll a, ll x, ll m){
   ll ret = 1;
   while(x){
      if(x%2){
         ret = ret*a%m;
      }
      a =  a * a%m;
      x /= 2;
   }
   return ret;
}

int main(){
    FASTIO;
    int _;cin>>_;
    while(_--){
        int n;cin>>n;
        vector<ll> arr(n);
        ll ans = -3;
        for(auto &x:arr){
            cin>>x;
            ans = max(ans, x);
        }
        int minusu = 0;
        int su2 = 0;
        int max2su = 0;
        for(int i=0;i<n;i++){
            if(arr[i]<0){
                minusu++;
                if(arr[i]==-2) su2++;
            }
            else if(arr[i]==2) su2++;
            else if(arr[i]==0){
                minusu = 0;
                su2 = 0;
                continue;
            }
            if(minusu%2==0){
                max2su = max(max2su, su2);
                ans = 1;
            }
        }
        minusu = 0;
        su2 = 0;
        for(int i=n-1;i>=0;i--){
            if(arr[i]<0){
                minusu++;
                if(arr[i]==-2) su2++;
            }
            else if(arr[i]==2) su2++;
            else if(arr[i]==0){
                minusu = 0;
                su2 = 0;
                continue;
            }
            if(minusu%2==0){
                max2su = max(max2su, su2);
                ans = 1;
            }
        }
        if(max2su!=0){
            ans = fastpow(2LL, max2su, MOD);
        }
        cout<<ans<<'\n';
    }
}