#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int m,n,k,w;
int mp[301][301];
int ans[301][301];
int tree[400001];

void add(int index){
	++index;
	while(index <= k+1){
		++tree[index];
		index += index & -index;
	}
}

void sub(int index){
	++index;
	while(index <= k+1){
		--tree[index];
		index += index & -index;
	}
}

int kth(int x){
	int ret = 0;
	for(int i=log2(400001); i>=0; --i){
		int pivot = 1<<i;
		if(ret + pivot <= k+1 && tree[ret+pivot] < x){
			x -= tree[ret+pivot];
			ret += pivot;
		}
	}
	return ret;
}

int main() {
    FASTIO;
    cin>>m>>n>>k>>w;
    for(int i=0;i<m;i++){
        for(int j=0;j<n;j++){
            cin>>mp[i][j];
            mp[i][j] += 1;
        }
    }
    int mid = (w*w+1)/2;
    for(int i=0;i<m-w+1;i++){
        for(int j=1;j<=k+1;j++) tree[j] = 0;
        for(int j=i;j<i+w;j++){
            for(int x=0;x<w;x++){
                int su = mp[j][x];
                add(su);
            }
        }
        for(int j=0;j<n-w+1;j++){
            ans[i][j] = kth(mid) - 1;
            for(int x=0;x<w;x++){
                int su = mp[i+x][j];
                sub(su);
            }
            for(int x=0;x<w;x++){
                int su = mp[i+x][j+w];
                add(su);
            }
        }
    }
    for(int i=0;i<m-w+1;i++){
        for(int j=0;j<n-w+1;j++) cout<<ans[i][j]<<' ';
        cout<<'\n';
    }
}