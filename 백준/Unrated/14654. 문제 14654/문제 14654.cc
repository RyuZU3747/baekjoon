#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
	int n;cin>>n;
	vector<int> arr(n);
	vector<int> brr(n);
	for(auto &x: arr) cin>>x;
	for(auto &x: brr) cin>>x;
	int ans = 0;
	int len = 0;
	int cur = 0;
	for(int i=0;i<n;i++){
		int u = arr[i];
		int d = brr[i];
		if((u == 3 && d == 1)){
			if(cur == 0) len = 1;
			else len++;
			cur = 1;
		}
		else if((d == 3 && u == 1)){
			if(cur == 1) len = 1;
			else len++;
			cur = 0;
		}
		else if(u<d){
			if(cur == 0) len = 1;
			else len++;
			cur = 1;
		}
		else if(d<u){
			if(cur == 1) len = 1;
			else len++;
			cur = 0;
		}
		else if(d==u){
			len = 1;
			cur ^= 1;
		}
		ans = max(ans, len);
	}
	cout<<ans;
}