#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int n;
vector<int> arr;

int main(){
    FASTIO;
    cin>>n;
    for(int i=0;i<n;i++){
        int a;cin>>a;
        arr.push_back(a);
    }
    int l = 0, r = n;
    int ans = 0;
    while(l<=r){
        int mid = (l+r)/2;
        int cnt[10] = {0,};
        int ok = 0;
        for(int i=0;i<mid;i++) cnt[arr[i]]++;
        int su = 0;
        for(int i=1;i<=9;i++){
            if(cnt[i]>0) su++;
        }
        if(su<=2){
            ok = 1;
        }
        for(int i=0;i<n-mid+1;i++){
            if(ok || i+mid>=n) break;
            su = 0;
            cnt[arr[i]]--;
            cnt[arr[i+mid]]++;
            for(int i=1;i<=9;i++){
                if(cnt[i]>0) su++;
            }
            if(su<=2){
                ok = 1;
            }
        }
        if(ok){
            l = mid+1;
            ans = mid;
        }
        else r = mid-1;
    }
    cout<<ans;
}