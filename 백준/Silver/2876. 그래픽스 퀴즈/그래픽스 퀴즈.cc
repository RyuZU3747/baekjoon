#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

vector<pii> arr;
int dp[101010][6];

int recur(int n, int g){
	int l=0, r=0;
	int ret = 0;
	while(r != n){
		if(arr[r].first == g || arr[r].second == g){
			r++;
		}
		else{
			r++;
			l = r;
		}
		ret = max(ret, r-l);
	}
	return ret;
}

int main(){
	FASTIO;
	int n;cin>>n;
	arr.resize(n);
	for(auto &x: arr) cin>>x.first>>x.second;
	pii ans = {0,0};
	memset(dp, -1, sizeof dp);
	for(int i=1;i<=5;i++){
		int t = recur(n, i);
		if(ans.first < t){
			ans.first = t;
			ans.second = i;
		}
	}
	cout<<ans.first<<' '<<ans.second;
}