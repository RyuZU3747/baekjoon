#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<int> arr(n);
    vector<int> brr(n);
    map<int, int> cnta;
    map<int, int> cntb;
    for(auto &x: arr){
        cin>>x;
        cnta[x]++;
    }
    for(auto &x: brr){
        cin>>x;
        cntb[x]++;
    }
    
    vector<int> ansa;
    vector<int> ansb;
    int cnt = 0;
    for(auto &e: cnta){
        while(cntb.count(e.first) && cntb[e.first] > 0 && e.second > 0){
            ansa.push_back(e.first);
            ansb.push_back(e.first);
            e.second--;
            cntb[e.first]--;
            cnt++;
        }
    }
    for(auto e: cnta){
        while(e.second > 0){
            ansa.push_back(e.first);
            e.second--;
        }
    }
    for(auto e: cntb){
        while(e.second > 0){
            ansb.push_back(e.first);
            e.second--;
        }
    }
    cout<<cnt<<'\n';
    for(auto e: ansa) cout<<e<<' ';
    cout<<'\n';
    for(auto e: ansb) cout<<e<<' ';
    cout<<'\n';
}