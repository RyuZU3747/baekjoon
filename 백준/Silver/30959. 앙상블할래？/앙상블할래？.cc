#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    int n,m;cin>>n>>m;
    vector<int> ans(m);
    for(auto &x: ans) cin>>x;
    vector<int> mod[n];
    int one = 0;
    for(int i=0;i<n;i++){
        for(int j=0;j<m;j++){
            int x;cin>>x;
            mod[i].push_back(x);
        }
        int t = 0;
        for(int j=0;j<m;j++){
            if(mod[i][j]==ans[j]) t++;
        }
        one = max(one, t);
    }
    
    for(int i=1;i<1<<(n);i++){
        if(__builtin_popcount(i) % 2){
            vector<int> tmp;
            for(int j=0;j<m;j++){
                int cnt1 = 0;
                int cnt0 = 0;
                for(int k=0;k<n;k++){
                    if(1<<k & i){
                        if(mod[k][j]==1) cnt1++;
                        else cnt0++;
                    }
                }
                if(cnt1 > cnt0) tmp.push_back(1);
                else tmp.push_back(0);
            }

            int t = 0;
            for(int j=0;j<m;j++){
                if(tmp[j]==ans[j]) t++;
            }
            if(one < t){
                cout<<1;
                return 0;
            }
        }
    }
    cout<<0;
}