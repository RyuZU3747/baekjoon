#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int _;cin>>_;
    while(_--){
        int a,b;cin>>a>>b;
        int r = a*3;
        if(r > b){
            cout<<r-b<<'\n';
            continue;
        }
        int cnt = 0;
        while(a > 0 && b > 0){
            a -= 1;
            b -= 3;
            cnt++;
        }
        if(a == 0){
            if(b <= cnt){
                cout<<0<<'\n';
                continue;
            }
            int ans = 0;
            while(b > cnt){
                ans ++;
                ans += max(0, 3-b);
                b -= 3;
                cnt++;
            }
            cout<<ans<<'\n';
            continue;
        }
        else{
            b -= cnt;
            cout<<(b+3)/4<<'\n';
        }
    }
}