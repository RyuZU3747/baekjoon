#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    int chair[n][m];
    for(int i=0;i<n;i++){
        for(int j=0;j<m;j++) cin>>chair[i][j];
    }
    int a;cin>>a;
    int sum = 0;
    for(int i=0;i<n;i++){
        for(int j=0;j<a;j++) sum += chair[i][j];
    }

    int mx = sum;
    for(int c=0;c<m-a;c++){
        for(int r = 0;r<n;r++){
            sum -= chair[r][c];
            sum += chair[r][c+a];
        }
        mx = max(mx, sum);
    }
    cout<<mx;
}