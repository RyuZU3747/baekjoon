#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    ll n,m,q;cin>>n>>m>>q;
    map<string, ll> hav;
    map<string, ll> comp;
    vector<string> grp[110];
    for(int i=0;i<n;i++){
        int g;cin>>g;
        string s;cin>>s;
        ll p;cin>>p;
        grp[g].push_back(s);
        comp[s] = p;
    }
    for(int i=0;i<q;i++){
        int x;cin>>x;
        if(x==1){
            string a;cin>>a;
            ll b;cin>>b;
            if(comp[a] * b <= m){
                m -= comp[a] * b;
                hav[a] += b;
            }
        }
        if(x==2){
            string a;cin>>a;
            ll b;cin>>b;
            if(hav[a] == 0) continue;
            if(hav[a] < b) b = hav[a];
            hav[a] -= b;
            m += b * comp[a];
        }
        if(x==3){
            string a;cin>>a;
            ll c;cin>>c;
            comp[a] += c;
        }
        if(x==4){
            int d;cin>>d;
            ll c;cin>>c;
            for(auto e: grp[d]){
                comp[e] += c;
            }
        }
        if(x==5){
            int d;cin>>d;
            ll e;cin>>e;
            for(auto t: grp[d]){
                if(e<0){
                    comp[t] = (double) comp[t] * (100.0 + e) / 100.0;
                    comp[t] /= 10;
                    comp[t] *= 10;
                }
                else{
                    comp[t] = (double) comp[t] * (100.0 + e) / 100.0;
                    comp[t] /= 10;
                    comp[t] *= 10;
                }
            }
        }
        if(x==6){
            cout<<m<<'\n';
        }
        if(x==7){
            ll t = 0;
            for(auto [c,h] : hav){
                t += comp[c] * h;
            }
            cout<<t+m<<'\n';
        }
    }
}