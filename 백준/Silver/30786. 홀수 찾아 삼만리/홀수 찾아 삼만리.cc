#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<pii> arr(n);
    vector<int> odd;
    vector<int> eve;
    for(int i=0;i<n;i++){
        cin>>arr[i].first>>arr[i].second;
        if((arr[i].first + arr[i].second) % 2 == 0) eve.push_back(i+1);
        else odd.push_back(i+1);
    }
    if(odd.size()==0 || eve.size()==0) cout<<"NO";
    else{
        cout<<"YES\n";
        for(auto e: eve) cout<<e<<' ';
        for(auto e: odd) cout<<e<<' ';
    }
}