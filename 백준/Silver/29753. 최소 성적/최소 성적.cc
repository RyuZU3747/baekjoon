#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;
    string t;
    cin>>n>>t;
    int x = (t[0]-'0')*100 + (t[2]-'0')*10 + (t[3]-'0');
    int div = 0;
    int cur = 0;
    for(int i=0;i<n-1;i++){
        int a;string b;cin>>a>>b;
        int score;
        if(b[0]=='A') score = 400;
        else if(b[0]=='B') score = 300;
        else if(b[0]=='C') score = 200;
        else if(b[0]=='D') score = 100;
        else if(b[0]=='F') score = 0;
        
        if(b[0]!='F' && b[1]=='+') score += 50;

        cur += a*score;
        div += a;
    }
    int c;cin>>c;
    div += c;
    double sco[10] = {0, 100, 150, 200, 250, 300, 350, 400, 450};
    int ans = -1;
    for(int i=0;i<9;i++){
        int newscore = cur + c*sco[i];
        int al = floor(1.0*newscore / div);
        if(al > x){
            ans = i;
            break;
        }
    }
    if(ans == 0) cout<<"F";
    else if(ans == 1) cout<<"D0";
    else if(ans == 2) cout<<"D+";
    else if(ans == 3) cout<<"C0";
    else if(ans == 4) cout<<"C+";
    else if(ans == 5) cout<<"B0";
    else if(ans == 6) cout<<"B+";
    else if(ans == 7) cout<<"A0";
    else if(ans == 8) cout<<"A+";
    else cout<<"impossible";
}
