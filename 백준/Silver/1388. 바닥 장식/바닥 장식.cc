using namespace std;

char mp[55][55];

int main(){
	FASTIO;
	int n,m;cin>>n>>m;
	int ans = 0;
	for(int i=1;i<=n;i++){
		for(int j=1;j<=m;j++){
			cin>>mp[i][j];
		}
	}
	for(int i=1;i<=n;i++){
		int cur = '|';
		for(int j=1;j<=m;j++){
			if(cur=='|' && mp[i][j]=='-') ans++;
			cur = mp[i][j];
		}
	}
	for(int i=1;i<=m;i++){
		int cur = '-';
		for(int j=1;j<=n;j++){
			if(cur=='-' && mp[j][i]=='|') ans++;
			cur = mp[j][i];
		}
	}
	cout<<ans;
}