#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int ans[4];
int curscore = 0;
int curadd = 1;
int curtime = 4;
int cur = 0;

void endgame(){
    if(curscore>=35&&curscore<65) ans[0]++;
    else if(curscore>=65&&curscore<95) ans[1]++;
    else if(curscore>=95&&curscore<125) ans[2]++;
    else if(curscore>=125) ans[3]++;
    cur = 0;
    curscore = 0;
    curadd = 1;
    curtime = 4;
}

int main(){
    FASTIO;
    int n;cin>>n;
    for(int _=0;_<n;_++){
        if(cur > 240) endgame();
        int d;cin>>d;
        if(d == 1){
            endgame();
        }
        if(d == 2){
            if(curadd > 1) curadd /= 2;
            else curtime += 2;
            curscore += curadd;
            cur += curtime;
        }
        if(d == 3){
            curscore += curadd;
            cur += curtime;
        }
        if(d == 4){
            cur += 56;
            curscore += curadd;
            cur += curtime;
        }
        if(d == 5){
            if(curtime > 1) curtime--;
            curscore += curadd;
            cur += curtime;
        }
        if(d == 6){
            if(curadd < 32) curadd *= 2;
            curscore += curadd;
            cur += curtime;
        }
    }
    for(int i=0;i<4;i++){
        cout<<ans[i]<<'\n';
    }
}