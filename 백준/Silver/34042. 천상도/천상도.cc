#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    while(m--){
        vector<ll> arr(n);
        ll ans = -1234567890123456789;
        int cnt1 = 0;
        int cnt2 = 0;
        for(auto &x: arr){
            cin>>x;
            ans = max(ans, x);
            if(x<0) cnt1++;
            if(x>0) cnt2++;
        }
        sort(arr.begin(), arr.end(),[](ll a, ll b){
            return abs(a) > abs(b);
        });
        ll t = -1234567890;
        if(cnt1>=2 || cnt2 > 0){
            t = 1;
            if(cnt1&1){
                for(auto e: arr){
                    if(e == 0 || (e < 0 && cnt1 == 1)) continue;
                    t *= abs(e);
                    if(e<0) cnt1--;
                }
            }
            else{
                for(auto e: arr){
                    if(e == 0) continue;
                    t *= abs(e);
                }
            }
        }
        cout<<max(ans, t)<<'\n';
    }
}