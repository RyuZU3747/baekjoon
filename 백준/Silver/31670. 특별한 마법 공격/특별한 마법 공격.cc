#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll dp[100001];
vector<ll> arr;
int n;

ll recur(int cur){
    if(cur < 0) return 0;
    ll & ret = dp[cur];
    if(ret != -1) return ret;
    ret = 1234567890123456789;
    if(cur == n) return min(recur(cur-1), recur(cur-2));
    ret = min(ret, recur(cur-1) + arr[cur]);
    ret = min(ret, recur(cur-2) + arr[cur]);
    return ret;
}

int main(){
    FASTIO;
    memset(dp, -1, sizeof dp);
    cin>>n;
    arr.resize(n);
    for(auto &x: arr) cin>>x;
    cout<<recur(n);
}