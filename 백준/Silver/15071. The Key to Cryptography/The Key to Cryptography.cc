#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    string msg;cin>>msg;
    string adds;cin>>adds;
    string ans = msg;
    int len = adds.length();
    for(int i=0;i<len;i++){
        int shft = adds[i] - 'A';
        char cur = msg[i] - shft;
        if(cur < 65) cur += 26;
        ans[i] = cur;
    }
    for(int i=len;i<msg.length();i++){
        int shft = ans[i - len] - 'A';
        char cur = msg[i] - shft;
        if(cur < 65) cur += 26;
        ans[i] = cur;
    }
    cout<<ans;
}