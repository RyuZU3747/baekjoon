#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    int n,m,k;cin>>n>>m>>k;
    map<string, int> cl;
    for(int i=0;i<n;i++){
        string s;cin>>s;
        int c;cin>>c;
        cl[s] = c;
    }
    int mnsc = 0;
    int mxsc = 0;
    for(int i=0;i<k;i++){
        string s;cin>>s;
        mnsc += cl[s];
        mxsc += cl[s];
        cl.erase(s);
    }
    vector<int> score;
    for(auto e: cl) score.push_back(e.second);
    sort(score.begin(), score.end());
    for(int i=0;i<m-k;i++){
        mnsc += score[i];
        mxsc += score[n-k-i-1];
    }
    cout<<mnsc<<' '<<mxsc;
}