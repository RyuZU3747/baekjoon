#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int sensor[1281];
int ans[361];

int main(){
    FASTIO;
    int n;cin>>n;
    for(int i=0;i<n;i++){
        int a,b;cin>>a>>b;
        a += 360 + 180;
        int s = (a-b);
        int e = (a+b);
        sensor[s] += 1;
        sensor[e+1] += -1;
    }
    for(int i=0;i<=1280;i++){
        sensor[i] += sensor[i-1];
    }
    for(int i=0;i<=1280;i++){
        if(sensor[i] != 0){
            ans[i%360] += sensor[i];
        }
    }
    int cnt = 0;
    for(int i=0;i<360;i++){
        if(ans[i]>=1){
            cnt++;
        }
    }
    cout<<cnt;
}