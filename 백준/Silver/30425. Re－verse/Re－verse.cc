#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

string s;
int n;
int sing[101];
char song[201];
int ans = 1;

void backtrac(int cur){
    if(cur==n) return;
    int no = 0;
    for(int i=0;i<n;i++){
        if(song[i+cur]==0){
            song[i+cur] = s[i];
        }
        else if(s[i] != song[i+cur]){
            no = 1;
            break;
        }
    }
    if(no==0) ans++;
    backtrac(cur+1);
}

int main(){
    FASTIO;
    cin>>n;
    cin>>s;
    for(int i=0;i<n;i++) song[i] = s[i];
    backtrac(1);
    cout<<ans;
}