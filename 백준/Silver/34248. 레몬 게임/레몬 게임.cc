#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
    int n;cin>>n;
    vector<int> arr(n);
    vector<int> stk;
    int sum = 0;
    int c2 = 0, c1 = 0;
    for(auto &x: arr){
        cin>>x;
        sum += x;
        if(x == 2) c2++;
        else c1++;
    }
    if(sum%3!=0 || c2 > c1) cout<<"No";
    else{
        for(int i=0;i<n;i++){
            if(stk.size()>0 && stk[stk.size()-1] + arr[i] == 3){
                stk.pop_back();
                continue;
            }
            stk.push_back(arr[i]);
        }
        while(stk.size()>2){
            if(stk[stk.size()-1] + stk[stk.size()-2] + stk[stk.size()-3] == 3){
                stk.pop_back();
                stk.pop_back();
                stk.pop_back();
            }
        }
        if(stk.size()==0) cout<<"Yes";
        else cout<<"No";
    }
}