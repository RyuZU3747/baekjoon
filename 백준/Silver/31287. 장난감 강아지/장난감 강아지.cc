#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,k;cin>>n>>k;
    string s;cin>>s;
    pii arr[2001];
    int r = 0, c = 0;
    for(int i=0;i<n;i++){
        if(s[i]=='U') r++;
        if(s[i]=='D') r--;
        if(s[i]=='R') c++;
        if(s[i]=='L') c--;
        arr[i] = {r,c};
    }
    pii mv = arr[n-1];
    int ok = 0;
    for(int i=0;i<min(2000, k);i++){
        pii t = {mv.first * i, mv.second * i};
        for(int j=0;j<n;j++){
            pii cur = {t.first + arr[j].first, t.second + arr[j].second};
            if(cur.first == 0 && cur.second == 0) ok = 1;
        }
    }
    if(ok) cout<<"YES";
    else cout<<"NO";
}