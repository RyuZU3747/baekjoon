#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int mp[5][5];
int R,C;
int dr[4] = {0,0,1,-1};
int dc[4] = {1,-1,0,0};
int vis[5][5];

int backtrac(int r, int c, int cnt, int app){
	if(cnt == 0){
		if(app>=2)return 1;
		return 0;
	}
	int ret = 0;
	for(int i=0;i<4;i++){
		int nr = r+dr[i];
		int nc = c+dc[i];
		if(nr<0||nr>4||nc<0||nc>4) continue;
		if(vis[nr][nc]||mp[nr][nc]==-1) continue;
		vis[nr][nc] = 1;
		if(mp[nr][nc] == 1) ret |= backtrac(nr,nc,cnt-1,app+1);
		else ret|= backtrac(nr,nc,cnt-1,app);
		vis[nr][nc] = 0;
	}
	return ret;
}

int main(){
	FASTIO;
	for(int i=0;i<5;i++){
		for(int j=0;j<5;j++) cin>>mp[i][j];
	}
	cin>>R>>C;
	vis[R][C] = 1;
	cout<<backtrac(R,C,3,0);
}