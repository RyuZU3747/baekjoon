#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
    int n,m;cin>>n>>m;
    vector<int> arr(n*m);
    map<int, int> cnt;
    for(auto &x: arr){
        cin>>x;
        cnt[x]++;
    }
    if(m&1){
        int h = 0;
        for(auto e: cnt){
            if(e.second&1) h++;
        }
        if(h > n || abs(h-n)&1) cout<<"NO\n";
        else cout<<"YES\n";
    }
    else{
        int h = 0;
        for(auto e: cnt){
            if(e.second&1) h++;
        }
        if(h!=0) cout<<"NO\n";
        else cout<<"YES\n";
    }
}