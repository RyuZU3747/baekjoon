#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
	int _=3;
	while(_--){
		string a, b;cin>>a>>b;
		map<int, int> acnt;
		map<int, int> bcnt;
		for(auto e: a) acnt[e-'0']++;
		for(auto e: b) bcnt[e-'0']++;
		auto chk = [&](){
			int ret = 1;
			for(int i=0;i<=9;i++){
				if(acnt[i] == 0 && bcnt[i] != 0) ret = 0;
				if(acnt[i] != 0 && bcnt[i] == 0) ret = 0;
			}
			return ret;
		};
		if(chk()){
			cout<<"friends\n";
			continue;
		}
		int ans = 0;
		for(int i=0;i<a.length()-1;i++){
			int c = a[i]-'0';
			int d = a[i+1]-'0';
			if(d != 0 && c!=9){
				acnt[c]--;
				acnt[c+1]++;
				acnt[d]--;
				acnt[d-1]++;
				if(chk()) ans = 1;
				acnt[c]++;
				acnt[c+1]--;
				acnt[d]++;
				acnt[d-1]--;
			}
			if(i==0 && c == 1) continue;
			if(c!=0 && d !=9){
				acnt[c]--;
				acnt[c-1]++;
				acnt[d]--;
				acnt[d+1]++;
				if(chk()) ans = 1;
				acnt[c]++;
				acnt[c-1]--;
				acnt[d]++;
				acnt[d+1]--;
			}
		}
		for(int i=0;i<b.length()-1;i++){
			int c = b[i]-'0';
			int d = b[i+1]-'0';
			if(d != 0 && c != 9){
				bcnt[c]--;
				bcnt[c+1]++;
				bcnt[d]--;
				bcnt[d-1]++;
				if(chk()) ans = 1;
				bcnt[c]++;
				bcnt[c+1]--;
				bcnt[d]++;
				bcnt[d-1]--;
			}
			if(i==0 && b[i] == 1) continue;
			if(c!=0&&d!=9){
				bcnt[c]--;
				bcnt[c-1]++;
				bcnt[d]--;
				bcnt[d+1]++;
				if(chk()) ans = 1;
				bcnt[c]++;
				bcnt[c-1]--;
				bcnt[d]++;
				bcnt[d+1]--;
			}
		}
		if(ans) cout<<"almost friends\n";
		else cout<<"nothing\n";
	}
}