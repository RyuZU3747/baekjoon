#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    string s;
    cin>>s;
    vector<char> stk;
    vector<char> ans;
    int len = s.length();
    for(int i=0;i<len;i++){
        if(s[i]=='('){
            stk.push_back('(');
            ans.push_back('(');
        }
        else if(s[i]==')'){
            stk.pop_back();
            if(s[i-1]=='(') ans.push_back('1');
            ans.push_back(')');
            if(i != len-1){
                if(s[i+1]=='(') ans.push_back('+');
            }
        }
    }
    for(auto e: ans) cout<<e;
}