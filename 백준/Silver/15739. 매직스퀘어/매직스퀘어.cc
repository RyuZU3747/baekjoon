#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    int mp[110][110];
    int sum = n*(n*n+1)/2;
    map<int, int> cnt;
    int no = 0;
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            cin>>mp[i][j];
            if(cnt.count(mp[i][j])) no = 1;
            cnt[mp[i][j]] = 1;
        }
    }
    for(int i=0;i<n;i++){
        int s = 0;
        for(int j=0;j<n;j++){
            s += mp[i][j];
        }
        if(s != sum) no = 1;
    }
    for(int i=0;i<n;i++){
        int s = 0;
        for(int j=0;j<n;j++){
            s += mp[j][i];
        }
        if(s != sum) no = 1;
    }
    int s = 0;
    for(int i=0;i<n;i++){
        s += mp[i][i];
    }
    if(s != sum) no = 1;
    s = 0;
    for(int i=0;i<n;i++){
        s += mp[i][n-i-1];
    }
    if(s != sum) no = 1;
    cout<<(no ? "FALSE" : "TRUE");
}