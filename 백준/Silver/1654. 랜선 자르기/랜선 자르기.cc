#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    ll k,n;cin>>k>>n;
    vector<ll> arr(k);
    for(auto &x: arr) cin>>x;
    ll left = 1, right = 100000000001;
    ll ans = 0;
    while(left<=right){
        ll mid = left+right>>1;
        ll sum = 0;
        for(auto e: arr) sum += e/mid;
        if(sum >= n){
            ans = max(ans, mid);
            left = mid + 1;
        }
        else right = mid - 1;
    }
    cout<<ans;
}