#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    string s;
    getline(cin, s);
    set<char> h,sa;
    h.insert('H');
    h.insert('A');
    h.insert('P');
    h.insert('Y');
    sa.insert('S');
    sa.insert('A');
    sa.insert('D');
    double ans = 0, hap = 0, sad = 0;
    for(int i=0;i<s.length();i++){
        if(h.find(s[i]) != h.end()) hap++;
        if(sa.find(s[i]) != sa.end()) sad++;
    }
    ans = 100*hap/(hap+sad) + (1e-9);
    if(hap+sad==0) ans = 50;
    cout<<fixed;
    cout.precision(2);
    cout<<ans;   
}