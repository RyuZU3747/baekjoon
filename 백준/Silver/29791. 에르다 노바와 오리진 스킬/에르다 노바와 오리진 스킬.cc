#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    int bef = 0;
    int acnt = 0;
    int bcnt = 0;
    vector<int> aa(n);
    for(auto &x:aa) cin>>x;
    sort(aa.begin(),aa.end());
    vector<int> bb(m);
    for(auto &x:bb) cin>>x;
    sort(bb.begin(),bb.end());
    for(int i=0;i<n;i++){
        int a = aa[i];
        if(i==0){
            bef = a;
            acnt++;
        }
        else{
            if(a - bef >= 100){
                bef = a;
                acnt++;
            }
        }
    }
    for(int i=0;i<m;i++){
        int a = bb[i];
        if(i==0){
            bef = a;
            bcnt++;
        }
        else{
            if(a - bef >= 360){
                bef = a;
                bcnt++;
            }
        }
    }
    cout<<acnt<<' '<<bcnt;
}