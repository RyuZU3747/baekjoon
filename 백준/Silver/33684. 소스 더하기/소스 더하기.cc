#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,k;cin>>n>>k;
    vector<int> arr(n);
    int mn = 1234567890, mx = -1234567890;
    for(auto &x:arr){
        cin>>x;
        mn = min(mn, x);
        mx = max(mx, x);
    }
    if(mx > k) cout<<0;
    else if(mn <= 0) cout<<-1;
    else{
        ll ans = 0;
        int no = 0;
        for(auto e: arr){
            if(no == 0 && e == mn){
                no = 1;
                continue;
            }
            ans += (k - e) / mn;
        }
        ans++;
        cout<<ans;
    }
}