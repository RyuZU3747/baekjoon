using namespace std;

vector<int> sosu;
bool arr[1000001];

void getsosu(){
    arr[1]=1;
    for(int i=2;i*i<=1000000;i++){
        for(int j=i*i;j<=1000000;j+=i){
            arr[j]=1;
        }
    }
    for(int i=1;i<1000001;i++){
        if(arr[i]==0){
            sosu.push_back(i);
        }
    }
    return;
}

