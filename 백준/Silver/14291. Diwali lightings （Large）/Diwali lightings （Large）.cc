#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
	int t;cin>>t;
	for(int _=1;_<=t;_++){
		cout<<"Case #"<<_<<": ";

		string s;cin>>s;
		ll a,b;cin>>a>>b;
		a--;b--;
		ll sb = 0;
		ll n = s.length();
		for(ll i=0;i<n;i++) if(s[i]=='B') sb++;
		ll ans = 0;
		s += s;
		ll ed = b%n;
		if(a/n != b/n) ed += n;
		for(ll i=a%n;i<=ed;i++){
			if(s[i]=='B') ans++;
		}
		ans += sb*max(0LL,(b/n - a/n - 1));
		cout<<ans<<'\n';
	}
}