#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int chk[101];

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    vector<pii> tae;
    char ans[101][101];
    int sum = 0;
    for(int i=0;i<m;i++){
        int a, b;cin>>a>>b;
        tae.push_back({a,b});
        sum += a;
        sum += b;
    }
    int xcnt = 0;
    vector<pii> xloc;
    for(int i=0;i<n;i++){
        for(int j=0;j<m;j++){
            if(tae[j].first > 0){
                ans[i][j] = '+';
                tae[j].first--;
            }
            else if(tae[j].second > 0){
                ans[i][j] = '-';
                tae[j].second--;
            }
            else if(xcnt < n){
                xcnt++;
                ans[i][j] = 'X';
                xloc.push_back({i,j});
            }
            else ans[i][j] = '+';
        }
    }
    
    int no = 0;
    for(int i=0;i<m;i++){
        if(tae[i].first > 0 || tae[i].second > 0) no = 1;
    }
    sort(xloc.begin(),xloc.end(), [](pii a, pii b){ return a.second < b.second;});

    if(no || sum > n*m-n || xcnt < n) cout<<"NO\n";
    else{
        cout<<"YES\n";
        int cur = 0;


        for(int i=0;i<n;i++){
            auto [x, y] = xloc[i];
            // while(ans[cur][y] == 'X') cur++;
            if(chk[x] == 0){
                chk[x] = 1;
                continue;
            }
            else{
                for(int j=0;j<n;j++){
                    if(chk[j] == 0){
                        swap(ans[j][y], ans[x][y]);
                        chk[j] = 1;
                        break;
                    }
                }
            }
        }


        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++) cout<<ans[i][j];
            cout<<'\n';
        }
    }
}