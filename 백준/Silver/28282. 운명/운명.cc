    map<ll, ll> left;
    map<ll, ll> right;
    for(int i=0;i<x;i++){
        int a;cin>>a;
        left[a]++;
    }
    for(int i=0;i<x;i++){
        int a;cin>>a;
        right[a]++;
    }
    map<ll, ll> rcnt;
    for(auto e: left){
        rcnt[e.first] = x - right[e.first];
    }
    ll ans = 0;
    for(auto l: left){
        ans += l.second * rcnt[l.first];
    }
    cout<<ans;
