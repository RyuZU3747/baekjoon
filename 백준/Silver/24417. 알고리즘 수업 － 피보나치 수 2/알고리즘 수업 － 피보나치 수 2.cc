#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll n;
ll a[2][2], ans[2][2], tmp[2][2], arr[2][2];

void multi(ll a1[][2],ll b[][2]){
	for(int i=0;i<2;i++){
		for(int j=0;j<2;j++){
			tmp[i][j]=0;
			for(int k=0;k<2;k++){
				tmp[i][j]+=a1[i][k]*b[k][j];
			}
		}
	}
	for(int i=0;i<2;i++){
		for(int j=0;j<2;j++){
			a1[i][j]=tmp[i][j]%MOD;
		}
	}
}

int main(){
	cin>>n;
	ans[0][0]=1;
	ans[1][1]=1;
	a[0][0]=1;
	a[0][1]=1;
	a[1][0]=1;
	arr[0][0]=1;
	arr[0][1]=1;
	arr[1][0]=1;
	int t = n-2;
	while(n){
		if(n%2==1) multi(ans,a);
		multi(a,a);
		n/=2;
	}
	
	cout<<ans[0][1]%MOD<<' '<<t;
}