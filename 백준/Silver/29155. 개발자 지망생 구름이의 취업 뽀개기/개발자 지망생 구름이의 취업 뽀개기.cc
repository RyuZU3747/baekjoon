#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    int dif[6];
    for(int i=1;i<=5;i++) cin>>dif[i];
    vector<pii> prob;
    for(int i=0;i<n;i++){
        int a,b;cin>>a>>b;
        prob.push_back({a,b});
    }
    sort(prob.begin(), prob.end(), [](pii a, pii b){return a.first == b.first ? a.second < b.second : a.first < b.first;});
    ll ans = 0;
    for(int i=0;i<n;i++){
        auto [d,t] = prob[i];
        if(dif[d]){
            ans += t;
            dif[d]--;
        }
        else continue;
        if(d==5&&dif[d]==0) break;
        if(dif[d]==0) ans += 60;
        else{
            ans += abs(t-prob[i+1].second);
        }
    }
    cout<<ans;
}