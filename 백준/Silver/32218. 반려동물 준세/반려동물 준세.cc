#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n;cin>>n;
    vector<int> arr(n);
    vector<int> brr(n);
    for(auto &x: arr) cin>>x;
    int ans = 0;
    while(1){
        ans++;
        brr = vector<int>(n,0);
        for(int i=0;i<n;i++){
            for(int j=i+1;j<n;j++){
                brr[i] += arr[i] < arr[j];
            }
        }
        int yes = 1;
        for(int i=0;i<n;i++){
            if(arr[i] != brr[i]) yes = 0;
        }
        if(yes) break;
        arr = brr;
    }
    cout<<ans;
}