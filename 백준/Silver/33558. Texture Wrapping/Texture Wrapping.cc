#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    int n,m;cin>>n>>m;
    int u,v;cin>>u>>v;
    vector<string> text;
    for(int i=0;i<u;i++){
        string t;cin>>t;
        text.push_back(t);
    }
    string meth;cin>>meth;
    char ans[201][201];
    if(meth == "clamp-to-edge"){
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                if(i >= u && j>=v){
                    ans[i][j] = text[u-1][v-1];
                }
                else if(i >= u){
                    ans[i][j] = text[u-1][j];
                }
                else if(j >= v){
                    ans[i][j] = text[i][v-1];
                }
                else ans[i][j] = text[i][j];
            }
        }
    }
    else if(meth == "repeat"){
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                ans[i][j] = text[i%u][j%v];
            }
        }
    }
    else{
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                if((i/u)%2 && (j/v)%2){
                    ans[i][j] = text[u-(i%u)-1][v-(j%v)-1];
                }
                else if((i/u)%2){
                    ans[i][j] = text[u-(i%u)-1][j%v];
                }
                else if((j/v)%2){
                    ans[i][j] = text[i%u][v-j%v-1];
                }
                else ans[i][j] = text[i%u][j%v];
            }
        }
    }
    for(int i=0;i<n;i++){
        for(int j=0;j<m;j++) cout<<ans[i][j];
        cout<<'\n';
    }
}