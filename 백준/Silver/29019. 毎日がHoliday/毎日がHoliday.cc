#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    while(1){
        int y,c,r;cin>>y>>c>>r;
        if(y==0&&c==0&&r==0) break;
        ll left = 0, right = 1000000000000;
        ll ans = 0;
        while(left <= right){
            ll mid = (left+right) / 2;
            ll money = mid;
            for(int i=0;i<y;i++){
                money -= c;
                if(money < 0){
                    money = -1;
                    break;
                }
                money = money * (1 + r/100.0);
                if(mid < money) break;
            }
            if(money < 0){
                left = mid+1;
            }
            else{
                right = mid-1;
                ans = mid;
            }
        }
        cout<<ans<<'\n';
    }
}