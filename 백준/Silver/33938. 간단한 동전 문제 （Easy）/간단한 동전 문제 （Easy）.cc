#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 998244353
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
	FASTIO;
    int n,m;cin>>n>>m;
    vector<int> arr(n);
    for(auto &x: arr)cin>>x;
    queue<pii> q;
    map<int, int> vis;
    vis[0] = 0;
    q.push({0,0});
    while(!q.empty()){
        auto [cur, cnt] = q.front();
        q.pop();
        if(cur == m){
            cout<<cnt;
            return 0;
        }
        for(auto e: arr){
            if(vis[cur+e]==1 || cur+e < -1000 || cur+e > 1000) continue;
            vis[cur+e] = 1;
            q.push({cur + e, cnt+1});
        }
    }
    cout<<-1;
}