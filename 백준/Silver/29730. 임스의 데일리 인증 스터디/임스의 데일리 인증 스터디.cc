#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    int n;cin>>n;
    string t;
    getline(cin,t);
    vector<string> stu;
    vector<int> boj;
    for(int i=0;i<n;i++){
        cin.clear();
        string s;getline(cin,s);
        if(s.length() > 7 && s.substr(0,7).compare("boj.kr/")==0){
            string t = s.substr(7,s.length()-7);
            boj.push_back(stoi(t));
        }
        else stu.push_back(s);
    }
    sort(stu.begin(),stu.end(),[](string a, string b){
        if(a.length() == b.length()) return a < b;
        else return a.length() < b.length();
    });
    sort(boj.begin(),boj.end());
    for(auto e: stu) cout<<e<<'\n';
    for(auto e: boj) cout<<"boj.kr/"<<e<<'\n';
}