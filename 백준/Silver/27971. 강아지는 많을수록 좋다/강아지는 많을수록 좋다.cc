#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int vis[100001];

int main(){
    int n,m,a,b;cin>>n>>m>>a>>b;
    vector<pii> res;
    for(int i=0;i<m;i++){
        int l,r;cin>>l>>r;
        res.push_back({l,r});
    }
    if(a<b) swap(a,b);
    queue<pii> q;
    q.push({0,0});
    int ans = -1;
    while(!q.empty()){
        auto [cur, cnt] = q.front();
        q.pop();
        if(cur==n){
            ans = cnt;
            break;
        }
        int nxt = cur + a;
        int no = 0;
        for(auto e: res){
            if(e.first <= nxt && nxt <= e.second) no = 1;
        }
        if(no==0 && nxt < 100001 && vis[nxt] == 0){
            q.push({nxt, cnt+1});
            vis[nxt] = 1;
        }
        nxt = cur + b;
        no = 0;
        for(auto e: res){
            if(e.first <= nxt && nxt <= e.second) no = 1;
        }
        if(no==0 && nxt < 100001 && vis[nxt] == 0){
            q.push({nxt, cnt+1});
            vis[nxt] = 1;
        }
    }
    cout<<ans;
}