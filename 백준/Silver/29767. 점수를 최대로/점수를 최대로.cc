#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define iii tuple<int,int,int>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

ll ans;
vector<ll> arr;
ll n,k;

int main(){
    FASTIO;
    cin>>n>>k;
    for(int i=0;i<n;i++){
        int a;cin>>a;
        arr.push_back(a);
    }
    vector<ll> sum(n+1);
    sum[0] = 0;
    for(int i=0;i<n;i++){
        sum[i+1] = sum[i] + arr[i];
    }
    sort(sum.begin()+1, sum.end(),[](ll a, ll b){return a>b;});
    ll ans = 0;
    for(int i=1;i<=k;i++) ans += sum[i];
    cout<<ans;
}