#pragma GCC optimize("O3")
#pragma GCC optimize("Ofast")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>
#define ll long long int
#define MOD 1000000007
#define pii pair<int,int>
#define pll pair<ll,ll>
#define FASTIO cin.tie(0),ios_base::sync_with_stdio(0)
using namespace std;

int main(){
    FASTIO;
    ll n;cin>>n;
    vector<ll> sudo(n);
    ll off[200001] = {0, };
    ll sum = 0;
    for(auto &x:sudo){
        cin>>x;
        sum += x;
    }
    cout<<sum<<'\n';
    ll q;cin>>q;   
    for(ll i=0;i<q;i++){
        ll a;cin>>a;
        if(a==1){
            ll b,c;cin>>b>>c;
            b--;
            if(off[b]){
                sudo[b] = c;
                cout<<sum<<'\n';
            }
            else{
                sum -= sudo[b];
                sum += c;
                sudo[b] = c;
                cout<<sum<<'\n';
            }
        }
        else{
            ll b;cin>>b;
            b--;
            if(off[b]){
                off[b] = 0;
                sum += sudo[b];
            }
            else{
                off[b] = 1;
                sum -= sudo[b];
            }
            cout<<sum<<'\n';
        }
    } 
}